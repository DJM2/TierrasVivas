                    <h2>¿Cómo llegar al Km 82 del Camino Inca?</h2>
                    <p class="p-salkantay">
                        La única forma de llegar a Piscacucho en Cusco es a través del Camino Inca 4 días / 3 noches.
                    </p>
                    <img src="{{ asset('img/blog/camino-inca-bastones-de-senderismo.webp') }}"
