                        una asombrosa caminata, con un arduo esfuerzo fisico. Pero con la gran satisfaccion de llegar a
                        Choquequirao. Las vistas de los paisajes son innolvidables e impresionantes. Durante la caminata
                        tendremos un guia profesional, cocinero, arrieros y caballos, quienes se encargaran de darle un
                        experiencia de viaje jamas contada. El guia le dara informacion durante toda el trayecto.
