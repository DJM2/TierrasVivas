                    <p class="p-salkantay">
                        La proximidad de Cachicata a Machu Picchu hace que la caminata a su alrededor sea una
                        alternativa atractiva al Camino Inca suscrito en exceso. Cachicata se encuentra en el Valle
                        Sagrado de los Incas, en esta caminata apreciará las terrazas de Huarocondo, la cascada de agua,
                        las ruinas de los Incas, las terrazas andinas, el Intipunku (Puerta del Sol), el camino inca
                        original, las canteras de piedra y las momias incas.<br>

                        El tercer día, continuará la caminata y llegará a Ollantaytambo, donde tomará el tren hacia el
                        pueblo de Aguas Calientes y pasará una noche en un hotel. El último día visitará la “Ciudad
                        Perdida de los Incas“. Tendrá una visita guiada a Machu Picchu y tiempo libre para caminar por
                        la ciudad.
                    </p>

                    <h2>Itinerario detallado a Cachicata</h2>
                    <h3>Día 01: Cusco – Huarocondo – Pachar – Corimarca</h3>
                    <p class="p-salkantay">
                        Conducimos desde Cusco a Pachar ubicado en el Valle Sagrado, llegaremos a Huaroncondo para
                        comenzar el sendero, aquí nos encontraremos con nuestro arriero y caballos y prepararemos el
                        equipo de campamento, luego subiremos las escaleras y visitaremos las terrazas de Huarocondor.
                        Continuamos la caminata hacia Raphcca, 2,900 m.s.n.m. (9,514 pies). Desde aquí caminamos por el
                        valle, a lo largo de la ruta apreciamos terrazas inka, canales de agua, comunidades quechuas y
                        flora y fauna endémicas. Seguimos un sendero empinado que eventualmente nos lleva a una cascada
                        alta donde podemos hacer una pausa para refrescar nuestro cuerpo y mente. Caminando más lejos,
                        visitamos el sitio inca poco conocido de Corimarca, nuestro primer campamento.
                    </p>
                    <h3>Día 02: Corimarca – Queñacancha</h3>
                    <p class="p-salkantay">
                        Subimos a través de prados abiertos, alcanzando una zona de matorrales de gran altitud conocida
                        como Puna, acercándonos al paso de Chancachuco 4.400 m.s.n.m. (14.435 pies). Esta parte de la
                        caminata ofrece vistas panorámicas de los picos más altos de la región de Cusco. Nuestra ruta
                        continúa a través de una meseta alta, pasamos lagos de montaña donde podemos ver aves andinas y
                        humedales, descendiendo a nuestro segundo campamento en Qeuñacancha, 4080 m.s.n.m. (13,385
                        pies).
                    </p>
