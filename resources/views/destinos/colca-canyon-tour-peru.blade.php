                    <p class="p-salkantay">
                        Alcanza una profundidad de 3.400 m, 62 millas (100 km) de largo. El Cañón del Colca se encuentra
                        entre uno de los cañones más profundos del mundo, junto con el Cañón de Cotahuasi. El valle fue
                        el pueblo natal de las chozas y Collagua hace casi 2000 años, quienes construyeron un sistema de
                        terrazas que capturan la escorrentía del agua de deshielo de los volcanes nevados cercanos. Los
                        galpones de colcas (grano) son testimonio de su presencia. Cuando llegaron los españoles
                        sometieron a los campesinos a trabajar en las minas y se trasladaron a la comunidad dispersa de
                        Collagua en 14 aldeas, cada una con una plaza principal y una hermosa iglesia colonial.
                    </p>
