                    <h2>Atractivos de Chinchero:</h2>
                    <p class="p-salkantay">

                        Flanqueada por grandes nevados como el Salkantay, Verónica y Soray, y las lagunas de Huaypo y
                        Piuray, Chinchero posee un rico patrimonio monumental y cultural, es uno de los pocos lugares
                        del
                        Cusco que conserva de manera visible el trazado urbano inca. , los restos del Palacio Inca.
                    </p>

                    <h3>La Iglesia de la Virgen de la Natividad en Chinchero</h3>

                    <p class="p-salkantay">
                        Se encuentra en la plaza de armas, esta es una de las grandes riquezas de las iglesias en los
                        pueblos del Cusco. Se terminó de construir en 1607.<br><br>
                        El nombre real de esta <strong>Iglesia de Chinchero</strong> es "Templo de la Señora de la
                        Natividad", aunque
                        inicialmente fue construida con el nombre de "Iglesia de Nuestra Señora de Monserrat", este
                        templo
                        es una construcción del siglo XVII.<br><br>
                        La <strong>Iglesia de Chinchero </strong> terminada en 1607 y es una de las primeras
                        edificaciones católicas
                        que
                        se construyó en el Perú, por lo que no es de extrañar que la Iglesia de Chinchero se construyera
                        sobre una antigua edificación inca, ya que forma parte de los templos que se levantaron con el
                        fin
                        para extirpar las idolatrías.
                    </p>

                    <h3>Arquitectónicamente, la Iglesia de Nuestra Señora de la Natividad en Chinchero</h3>
                    <p class="p-salkantay">
                        Es una estructura que mezcla las preferencias arquitectónicas incaicas e hispánicas, lo cual es
                        evidente al observar sus cimientos, en contraste con su fachada, planta y divisiones
                        internas.<br><br>
                        En su interior, la <strong>Iglesia de Chinchero</strong> destaca por los impresionantes murales
                        de filigrana que
                        posee y que tiene motivos religiosos. Entre las piezas religiosas de esta iglesia se destaca un
                        mural de la Virgen de Monserrat, en el templo también es posible encontrar pinturas de carácter
                        histórico como la derrota de Túpac Amaru, entre otras.<br><br>
                        Otras piezas que llaman la atención de la decoración interna del templo, es el altar que posee y
                        que presenta un estilo barroco indiscutible, este altar llama la atención por su decoración en
                        pan de oro.
                    </p>
                    <h3>Casa Hacienda de Mateo Pumacahua</h3>
                    <p class="p-salkantay">
                        Esta hacienda perteneció al curaca de Chinchero, Mateo Pumacahua, el cacique más poderoso del
                        Perú en la época del siglo XVIII. Nació en este mismo pueblo en 1748, y fue partícipe de la
                        causa indígena en 1814.
                    </p>
                    <h3>Lagunas de Piuray y Huaypo</h3>
                    <p class="p-salkantay">
                        Huaypo una hermosa laguna de aguas cristalinas, ubicada entre las provincias de Urubamba y Anta.
                        Huaypo alberga una gran variedad de peces y aves en sus aproximadamente 1,5 km por 800 m de
                        longitud, totalmente recomendable para la práctica de deportes acuáticos planos.<br><br>
                        Piuray tiene un perímetro de 8.750 m, 465 m de ancho y 3.000 m. de largo, una profundidad entre
                        20 y 50 m. <br>Esta laguna abastece de agua a la cosmopolita ciudad del Cusco desde el
                        Tahuantinsuyo. Fueron los Incas quienes llevaron sus aguas a la ciudad imperial a través de
                        acueductos subterráneos. <br>Según una de las leyendas locales, el Dios Sol le pidió a Manco
                        Cápac a
                        sus hijos mellizos para que lo acompañaran en su atardecer, pero cuando fueron a buscarlos
                        descubrieron que el hijo que caminó más lejos se había convertido en la laguna de Huaypo y el
                        hija en la laguna de Piuray.
                    </p>
                    <h3>Mercado en Chinchero Perú</h3>
                    <p class="p-salkantay">
                        El <strong>mercado de Chinchero Perú </strong> uno de los más importantes de la región debido a
                        la alta calidad
                        de los productos artesanales que allí se venden, reconocidos internacionalmente por su excelente
                        trabajo y belleza. En estas ferias podemos ver que el trueque (antigua forma de intercambio de
                        productos) sigue vigente; En el <strong>mercado de Chinchero Perú</strong>, las nietas de las
                        princesas incas y
                        las tejedoras de Chinchero continúan tejiendo lanas y confeccionando prendas, como hacían las
                        ajllas para los hijos del Sol, en el Cusco imperial. Se lleva a cabo todos los domingos en la
                        Plaza de Armas del distrito, donde la mayoría de los tejedores de Chinchero venden sus
                        productos.
                    </p>
                    <h3>Zona Arqueológica de Chinchero</h3>
                    <p class="p-salkantay">
                        En el museo del Sitio Arqueológico de Chinchero el visitante podrá encontrar una colección de
                        bienes culturales, principalmente muebles que fueron encontrados en su mayor parte en
                        excavaciones arqueológicas en las ruinas de Chinchero en Perú.<br> Asimismo, cerámica ceremonial
                        y
                        utilitaria, metal lítico, objetos pétreos como morteros para moler granos y plantas, un gran
                        trozo de piedra tallado en forma de puma, vestimenta típica de la zona, restos óseos, una
                        colección etnográfica compuesta por instrumentos de labranza. , de tejidos y otros
                        materiales.<br><br>
                        En una segunda sala se encuentra una importante colección de pinturas de la Escuela Cusqueña,
                        pintura colonial de los siglos XVII y XVIII, y dos maniquíes ataviados con trajes típicos de la
                        región.<br><br>
                        Sugerimos una visita al mercado dominical, la hermosa iglesia colonial y la Zona Arqueológica de
                        Chinchero que rodean la plaza.
                    </p>

