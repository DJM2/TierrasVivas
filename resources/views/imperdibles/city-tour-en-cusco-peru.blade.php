                    <h2>Q'enqo:</h2>
                    <p class="p-salkantay">
                        Ubicado a cuatro kilómetros al noreste de la ciudad del Cusco, Qenqo es un centro ritual
                        construido sobre un singular afloramiento de roca caliza, con galeras subterráneas y un
                        anfiteatro semicircular cuenta con los grupos arqueológicos de Q’enqo Grande y Q´enqo
                        Chico.Etimológicamente, significa laberinto o zigzag, cumplió una función netamente religiosa,
                        encontrándose en la parte exterior un anfiteatro en forma semicircular con nichos trapezoidales
                        y un monolito que simula ser un puma el cual mide aproximadamente seis metros.
                    </p>
                    <h2>Pucapucara:</h2>
                    <p class="p-salkantay">
