                    <p class="p-salkantay">
                        “Padre Sol, mi Padre”, grita el Inca mientras preside la ceremonia del Inti Raymi en el Cusco,
                        en honor al Dios Sol Inca. Inti, el Dios del sol, fuente de todas las riquezas, rey del cielo,
                        las plantas y el universo se limita a su cabeza. El Dios Sol Inca, la Luna y las estrellas
                        formaron el núcleo del panteón Inca.<br><br>

                        Inti está estrechamente relacionado con la agricultura, ya que el sol como cuerpo celeste
                        proporciona a los campos la luz y el calor que necesitan para cultivar.
                    </p>
                    <h2>¿Quién era el Dios Sol Inca?</h2>
                    <p class="p-salkantay">
                        Se creía que Inti era hijo de Wiracocha, el creador del universo y su esposa, Mama Cocha, la
                        Diosa del mar. En cierta versión de una leyenda Inca, Wiracocha tenía dos hijas además de Inti:
                        Pachamama y
                        Mama Quilla. La primera fue adorada como diosa de la tierra, mientras que la segunda fue la
                        diosa de la luna.<br><br>

                        Mama Quilla era considerada por los incas como la esposa de Inti, además de su hermana. Inti
                        ejerció una poderosa influencia en la vida de los incas. Creían que Inti controlaba sus
                        actividades agrícolas, por lo que era vital para su existencia.<br><br>

                        Aunque Inti solía ser un Dios benevolente y generoso, también era capaz de enojarse
                        terriblemente, y los eclipses solares se consideraban una manifestación de su descontento.
                    </p>

                    <h2>Templo del Sol Inca en Cusco</h2>
                    <p class="p-salkantay">
                        Los historiadores creen que el Templo del Sol Inca en Cusco fue construido durante el reinado de
                        Pachacuteq Inca Yupanqui, noveno emperador Inca.<br>

                        Qoricancha o Coricancha fue destruida por los conquistadores Españoles, por el oro que tenia en
                        el templo y la mayoría de sus piedras se usaron para construir la iglesia del mismo nombre.
                    </p>
                    <h2>¿Donde esta ubicación el Templo Inca del Sol?</h2>
                    <p class="p-salkantay">
                        La ubicación del Templo del Sol Inca, llamado Coricancha (palabra que significa “Casa del Sol”),
                        ubicado en la antigua capital Inca de Cusco, estaba dedicada al Dios Sol.
                    </p>
