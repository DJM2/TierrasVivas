                    <p class="p-salkantay">
                        El río Urubamba se origina en las montañas de la región de Puno y su río arriba se llama
                        Willkanuta, que significa la “Casa del Sol” en lengua nativa aymara, luego fue hispanizado a
                        Vilcanota. Luego, el río fluye hacia el noroeste durante 724 km y cambia su nombre a Urubamba en
                        la provincia de Convención. A veces, también se puede escuchar que el río se llama Willcamayu,
                        que es su nombre quechua. El valle fue muy apreciado por los incas también por sus cualidades
                        geográficas y climáticas.
                        Fue uno de los principales lugares de producción por la fertilidad de su suelo. ¡El valle es un
                        lugar donde se cultiva el mejor maíz peruano! <br><br>

