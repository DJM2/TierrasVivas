                    <h3>Dia 04: Cusco</h3>
                    <p class="p-salkantay">
                        Viaje amazónico, lo trasladamos de regreso al aeropuerto de Puerto Maldonado para su vuelo al
                        ombligo del mundo, Cusco!!! A su llegada a Cusco, nos reuniremos y le daremos la bienvenida
                        nuevamente a la gran ciudad imperial del Cusco para luego trasladarlo al Hotel. El resto del día
                        es libre para aclimatarse a su gran altura y descubrir la ciudad a su conveniencia.<br><br>

                        Cusco fue la capital y el corazón del Imperio Inca (Tawantinsuyo = el País de los Cuatro Puntos
                        Cardinales) que sirvió como centro para la expansión Inca en nuevos territorios. También es el
                        activo arqueológico de las Américas. La ciudad habitada más antigua del continente. La
                        enigmática ciudad de Cusco está impregnada de un sentimiento de misterio e historia. Puede ver
                        un estilo español en la ciudad de hoy con su encantador techo de tejas rosas, plazas arqueadas y
                        callejuelas empinadas y curvas. La ciudad se levanta sobre enormes cimientos incas, mientras
                        caminas por la ciudad no puedes perderte la conexión del Imperio Inca, desde los muros de piedra
                        que bordean las calles hasta los museos llenos de antigüedades y reliquias de comunidades
                        desaparecidas.
                    </p>
                    <img src="{{ asset('img/galeria/cusco-ciudad-imperial.webp') }}" alt="Plaza de armas del Cusco"
                        loading="lazy" width="100%"><span>Foto: Plaza de armas del Cusco</span><br><br>
                    <h3>Día 05: Valle Sagrado de los Incas</h3>
                    <p class="p-salkantay">
                        En este día, visitará las ruinas incas del Valle Sagrado . El Valle Sagrado fue el principal
                        proveedor de alimentos del imperio Inca y hoy se pueden apreciar varios remanentes incas. El
                        valle ha sido formado por el río Urubamba también conocido como el río Vilcanota y pasa por
                        Pisac y Ollantaytambo hasta el antiguo santuario natural de Machu Picchu.. El Valle Sagrado es
                        una exuberante región agrícola que sigue abasteciendo a la ciudad de Cusco con gran parte de su
                        producción. Nos llevan al impresionante sitio Inca de Pisac, un complejo arqueológico situado en
                        la ladera sobre la ciudad, con increíbles vistas de las montañas cubiertas de escarcha. Este
                        lugar tiene fascinantes terrazas agrícolas talladas en la ladera de la montaña. Cuesta arriba
                        hay templos bien cuidados, incluido un Reloj Solar o Intiwatana, que son diseños
                        magníficos.<br><br>

                        Continuando con nuestro recorrido por Pisac, nos dirigiremos a su mercado tradicional. ¡Una
                        excelente ubicación para comprar algunos recuerdos! Tenemos suficiente tiempo libre aquí para
                        ver y comprar algunas artesanías o probar algunas comidas clásicas andinas, luego haremos una
                        parada en Urubamba, considerada la capital del valle, para tener un almuerzo buffet con cocina
                        andina (no incluida en el tour). Luego continuaremos hacia Ollantaytambo, un pequeño pueblo
                        rebosante de ambiente nativo y ocupado desde la época de los Incas, paramos en el templo
                        principal que fue utilizado como fortaleza inca durante la resistencia inca. Aquí disfrutarás de
                        hermosas vistas y podrás tomar algunas fotos increíbles.<br><br>

                        Nos llevarán al hotel donde podremos tener un tiempo de inactividad por el resto del día en este
                        pequeño pueblo antes de caminar a Machu Picchu a la mañana siguiente. Pernocte en Ollantaytambo.
                    </p>
                    <h3>Día 06-09: Caminata por el Camino Inca a Machu Picchu</h3>
                    <p class="p-salkantay">
                        Los tours del Camino Inca de 4 días son físicamente exigentes pero vale la pena. Todo un sistema
                        de conexiones de caminos de unión unía al Cusco con sus colonias y sus demás mancomunidades.
                        Esta maravillosa ruta va desde las altas montañas hasta el bosque nuboso, pasando por zonas
                        ecológicas con una variedad de flora y vida silvestre y una variedad de sitios arqueológicos que
                        solo se pueden ver en esta ruta. Habrá nuestra cocina y porteadores para llevar la mayor parte
                        del equipo, por lo que solo necesita traer una pequeña mochila. Está dentro de la capacidad del
                        más razonablemente apto. Esta caminata de 44 km (27 millas) a menudo es empinada con 2 pasos
                        altos para cruzar. Las temperaturas nocturnas pueden caer por debajo de cero, por lo que es
                        importante estar preparado para eso. A veces también llueve, incluso en la estación seca.
                    </p>
                    <img src="{{ asset('img/galeria/camino-inca-a-machu-picchu-cusco.webp') }}"
                        alt="Camino Inca a Machu Picchu" loading="lazy" width="100%"><span>Foto: Trayectoria al camino
                        inca a Machu Picchu</span><br><br>
                    <h3>Día 10: Cusco</h3>
                    <p class="p-salkantay">
                        Hoy tiene tiempo para explorar a su gusto, quizás visitando algunos de los sitios a pie o en
                        autobús. Para los fanáticos de la arqueología, los sitios incas de Q’enqo. Para los más
                        aventureros hay actividades opcionales como rafting en el río Urubamba (grado 2-3) o ciclismo de
                        montaña en el Valle Sagrado de los Incas de Urubamba o Maras – Moray – Salineras y quizás
                        visitar algunos sitios incas en el camino. No es necesario tener experiencia para realizar estas
                        actividades, se dan instrucciones y se proporciona todo el equipo de seguridad.<br><br>

                        También puede visitar las cuatro ruinas incas circundantes a lo largo del camino: Q’enqo, Puka
                        Pukara, Tambomachay y la espectacular fortaleza de Sacsayhuaman , construida de manera
                        escalonada en una colina que domina el Cusco , que se distingue por sus enormes piedras
                        talladas, unidas con asombrosa precisión para formar el paredes exteriores, algunas de las
                        cuales tienen más de 9 m / 30 pies de altura y pesan más de 350 toneladas. También puedes
                        realizar paseos a caballo en estos sitios.
                    </p>
