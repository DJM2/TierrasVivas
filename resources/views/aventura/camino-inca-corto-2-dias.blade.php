                    <h2>Montaña de Huayna Picchu y Machu Picchu</h2>
                    <p class="p-salkantay">
                        La Montaña <strong> Machu Picchu,</strong> también conocida como la ‘Montaña Vieja’, es la
                        montaña sobre la que se
                        construyó la ciudadela Inca. Se encuentra ubicada a una altitud de 3.082 metros, que casi 600
                        metros sobre la ciudad Inca. Machu Picchu se denominó después de esta montaña.<br><br>

                        En la actualidad, la Montaña Machu Picchu es capaz de recibir a 200 turistas diarios, quienes
                        ascienden por el ancho sendero montañoso mientras experimentan impresionantes vistas de toda la
                        geografía del lugar. El camino es ideal para visitantes familiares ya que no es empinado ni
                        tiene quebradas vertiginosas, a diferencia de Huayna Picchu. ¡Para poder visitar la montaña es
                        necesario adquirir con anticipación un Boleto Machu Picchu + Montaña!<br><br>

                        Huayna Picchu, o la ‘Montaña Joven’, es una montaña situada al norte de la ciudadela Inca de
                        Machu Picchu. Es la cumbre que destaca en la clásica postal de las Maravillas del Mundo y se
                        encuentra a una altitud de 2.693 metros, 300 metros más alta que la ciudad Inca. En el camino
                        hacia arriba, se encuentra el Templo de la Luna, Templo de la Luna, un sitio religioso tallado
                        en la piedra de una cueva.<br><br>

                        La camino al Huayna Picchu es solicitada por la mayoría de los visitantes aventureros ya que
                        disfrutan tomándose selfies allí. Para llegar a su cima, uno debe ascender por un camino
                        estrecho hasta los márgenes de los acantilados de vértigo para ser recompensado con espléndidas
                        vistas de la Nueva Maravilla del Mundo al final. La escalada es considerada una de las caminatas
                        cortas más increíbles del mundo. Sin embargo, solo 200 visitantes pueden ingresar
                        diariamente.<br><br>

                        ¡Para ascender a la Montaña Huayna Picchu, necesita comprar un Boleto Machu Picchu + Huayna
                        Picchu de antemano! En caso de reservar con Tierras Vivas, nuestro departamento reservara su
                        ticket de ingreso solicitado segun disponibilidad.
                    </p>
