                        Para comenzar Lares Trek a Machu Picchu. Muy temprano en la mañana los recogeremos del hotel
                        saldremos de Cusco a las 5:30 a.m. en un transporte turístico en dirección a las aguas termales
                        de Lares, nos tomará aproximadamente 4 horas. Durante el viaje, pasaremos por el Valle Sagrado
                        de los Incas, hermosos pueblos y paisajes espectaculares.
                        Cuando lleguemos a las aguas termales de Lares, disfrutaremos de un excelente relajo y aquí
                        tendremos nuestro delicioso almuerzo. Después del almuerzo, comenzaremos a caminar hacia la
                        pequeña comunidad de Wacawasi, donde pasaremos nuestra primera noche de campamento con familias
                        locales.
                        La caminata nos llevará aproximadamente 4 horas por una ruta moderada, cuando lleguemos
                        descansaremos en nuestras carpas, a las 7:00 pm nos reuniremos para cenar bajo las interminables
                        estrellas.
                    </p>
