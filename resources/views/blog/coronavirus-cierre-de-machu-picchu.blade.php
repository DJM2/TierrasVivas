                    <h2>¿Cuales son los medios de transporte para ir a Machu Picchu?</h2>
                    <h3>Viaje en autobús</h3>
                    <p class="p-salkantay">
                        A menos que camine por el Camino Inca, primero debe tomar un tren a Machu Picchu. Antes de
                        llegar a pueblo de Aguas Calientes, debe bajarse en el km 104. Punto de inicio de Camino Inca.
                        De alli iniciar la Caminata de 2 Dias a Machu Picchu. Esta ruta es la mas corta y exuberante de
                        la ruta Inca. La ruta mas conocida y aventurera es el Camino Inca de 4 Dias, la caminata incluye
                        cocinero, porteadores y un guia profesional. Ambas rutas necesitan un operador
                        turistico.<br><br>

                        En caso de continuar en tren a Machu Picchu, hay un autobús turístico oficial de Consettur que
                        te lleva a las famosas ruinas. Si es un excursionista, siga a la multitud que sale de la
                        estación de tren a unas dos cuadras, encontrara la estacion de buses de Consettur Machu Picchu,
                        los buses lo llevaran a través de una serie de curvas hacia el centro arqueoligico, un viaje de
                        20 minutos. Antes de abordar, compre un boleto de ida y vuelta de USD12 (USD7 para niños). Los
                        boletos de autobús se pueden comprar en dólares estadounidenses o soles peruanos. Si va a pasar
                        la noche, primero regístrese en su alojamiento y luego regrese para comprar un boleto de
                        autobús. Los autobuses salen de Aguas Calientes hacia las centro arqueoligico apartir de las
                        5:30 a.m. y luego salen aproximadamente cada 10 minutos. La multitudes de viajeros aparecen a
                        media mañana desde que llegan los trenes. El sitio histórico cierra alrededor de las 5:30 p.m.
                        Si regresa a Cusco, tome un autobús de enlace de regreso al menos una hora antes de la salida
                        del tren. También es posible caminar hacia y desde las ruinas a Aguas Calientes, pero esta
                        caminata te toma una buena hora y media de cualquier manera (el camino es empinado).<br><br>

                        La segunda opcion de viaje a Machu Picchu en bus, es por la famosa Ruta Amazonica. Conocida asi,
                        por el ingreso a la ceja de Selva del Cusco. El viaje inicia en la ciudad de Cusco, muy
                        temprano, abordamos un bus de Cusco a Hidrolectrica, este viaje en un bus turistico dura
                        aproximadamente 7 horas, durante el viaje podra apreciar el Valle Sagrado de los Incas, la
                        sierra del Cusco con bello parajes y hermosas vistas de postales, el Abra Malagra (4550 m.s.n.m)
                        y el impomente nevado de la Veronica, a la vista de los ojos de un aventurero. Luego
                        decenderemos hacia la ceja de selva del Cusco. Con su clima cambiantes de frio a calor, de
                        sierra a selva, vistas impresionantes durante el viaje.<br><br>

                        Al llegar a Hidroeléctrica, iniciaremos un caminata de 3 horas al pueblo de Machu Picchu,
                        durante el trayecto apreciaremos el Intiwatana ubicado en la parte alta de la pueblo de
                        Hidrolectrica, cruzaremos el puente de Mandor y apreciaremos el rio Vilcanota, Continuando la
                        caminata por la linea ferrea, apreciaremo la flora y fauna de la ruta Amazonica.

                        Una vez en el pueblo de Machu Picchu, necesitara relajarse en su hotel o dirigirse a las baños
                        termales de Machu Picchu. Al dia siguiente ascender a la ciudadela Inca.
                    </p>
