                    <h3>Dia 02 – 03: Viaje a la Selva Peruana</h3>
                    <p class="p-salkantay">
                        Trasladado desde su hotel al aeropuerto de Lima para tomar un vuelo al aeropuerto de Puerto
                        Maldonado, se encuentra en las profundidades de la Amazonía Peruana. Una vez en Puerto
                        Maldonado, nos dirigiremos al puerto fluvial de Tambopata, donde viajará hacia la profunda
                        Amazonía peruana en un bote motorizado sobre el río Madre de Dios hasta nuestro albergue ubicado
                        cómodamente en la selva. En nuestro viaje hacia el albergue, ingresaremos en la zona de
                        amortiguamiento de esta unidad de conservación de 1.3 millones de hectáreas, donde tendrá la
                        oportunidad de ver especies de aves típicas del río y las afueras del bosque.<br><br>

                        El Lodge en la selva combina una arquitectura y materiales nativos con una tecnología de bajo
                        impacto con la intención de preservar el medio ambiente local . Las habitaciones del albergue
                        son hogareñas y muy cómodas, contienen mosquiteros sobre cada cama, inodoros con cisterna,
                        regaderas (sin agua caliente y realmente te encantará el agua fría) y lámparas de gas propano
                        para la iluminación (no hay electricidad), entonces esta es la verdadera aventura!!!. El
                        personal del albergue son naturalistas, profesionales del turismo y miembros de la comunidad que
                        hablan inglés. Recuerda que explorarás la Amazonía peruana con guías locales y nativos.
                    </p>
