                        se encuentra un punto de control del Camino Inca. ¡Cruzamos el río Vilcanota por un puente y
                        comenzamos el Camino Inca! El primer sitio arqueológico para visitar es Chachabamba. Luego
                        continuamos a través de una mezcla de vegetación de cactus y un bosque nuboso hacia otro sitio
                        significativo inca: Wiñayhuayna. Pero antes de llegar, disfrutamos de la frescura de una cascada
