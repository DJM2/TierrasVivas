                    <h2>Itinerario Detallado Salkantay 5 días</h2>
                    <h3><strong>Día 01:</strong> Cusco – Soraypampa</h3>
                    <p class="p-salkantay">
                        Comenzamos el día con el recojo de su hotel a partir de las 05:00 am, luego iremos en un bus
                        turístico pasando por las localidades de Izcuchaca y Limatambo arribando a Mollepata (2,900
                        m.s.n.m) donde estaremos aproximadamente a las 7:30 am donde tendremos nuestro primer desayuno,
                        nos encontraremos con el arriero y podremos dejar nuestros 05 kilos de nuestro equpo personal,
                        luego tomaremos un transporte que nos llevara hasta Challacancha por 1 hora (3,380m.s.n.m),
                        desde ese punto empezaremos nuestra caminata de 3 horas aproximadamente, llegando a Soraypampa
                        (3,900 m.s.n.m) donde estaremos arribando a la 1:30 pm ahí tomaremos nuestro primer almuerzo y
                        primer campamento,

                        Despues del almuerzo ascederemos a la hermosa Laguna de Humantay, donde tendremos una vista
                        impresionante de la montaña de Salkantay, Humantay y la colorida Laguna. Por la tarde
                        descendremos al campamento.
                    </p>
                    <h3><strong>Día 02:</strong> Soraypampa – Salkantay – Chaullay</h3>
                    <p class="p-salkantay">
                        Entre las 5:00 am nos dispondremos a degustar de un nutritivo desayuno para luego continuar con
                        nuestra caminata, iniciamos desde el punto de Soraypamapa hasta el punto más alto, el Abra
                        Salkantay (4 horas) donde podremos apreciar los imponentes nevados de las montañas Salkantay,
                        Humantay, Tucarhuay y Pumasillo (4,650 m.s.n.m) pasando por Salkantaypampa y Soyrococha donde
                        apreciaremos una pequeña laguna luego empezaremos el descenso hasta Huayracmachay (3 horas)
                        (3,800 m.s.n.m), donde tomaremos nuestro almuerzo luego seguiremos la caminata donde notarás un
                        cambio dramático en el paisaje, de Sierra a Ceja de Selva, esta caminata es realmente
                        impresionante después continuamos bajando hasta Chaullay (3 horas) (2,900 m.s.n.m) donde será
                        nuestro segundo campamento.
                    </p>
                    <h3><strong>Día 03:</strong> Chaullay – Playa Sahuayaco – Santa Teresa</h3>
                    <p class="p-salkantay">
                        Tomaremos nuestro desayuno a las 06:00 am, seguidamente caminaremos por el tiempo 05.30 horas
                        todo por nuestra ceja de selva con ascensos y descenso llanos, en este trayecto apreciaremos
                        plantaciones de café, orquídeas, bromelias y muchas plantas naturales utilizadas para la
                        medicina natural, llegaremos a nuestro punto de descanso Playa Sahuayaco donde tendremos el
                        almuerzo y abordaremos un transporte, el cual está incluido de Playa Sahuayaco al campamento de
                        Santa Teresa. Arribando a nuestro campamento tendremos la opción de visitar los baños termales
                        ubicados en Cocalmayo (Santa Teresa) que no están incluidos en nuestro paquete y retornamos a
                        nuestro campamento a las 06:00 pm, para nuestro té y cena.
                    </p>
                    <h3><strong>Día 04:</strong> Santa Teresa – Hidroeléctrica – Aguas Calientes</h3>
                    <p class="p-salkantay">
                        El amanecer será dentro de una zona tropical lleno de historia y naturaleza, el desayuno estara
                        listo para degustar y luego continuaremos nuestro viaje con dirección hacia Hidroeléctrica donde
                        tenemos el almuerzo en el restaurante, después del almuerzo continuaremos nuestra caminata por
                        zona tropical hacia el pueblo de Machu Picchu y acomodarnos en el hostal y esperar la cena a las
                        6:30 pm.
                        Durante el dia, tendremos la opcion de hacer Zip Line o Tiroleza, esta actividad no esta
                        incluida dentro del servicio es costo extra.
                    </p>
                    <h3><strong>Día 05:</strong> Aguas Calientes – Machu Picchu – Cusco</h3>
                    <p class="p-salkantay">
                        Nos levantaremos muy temprano para tener la mejor visita de Machu Picchu, tendremos una visita
                        guiada de la ciudadela de Inca por los templos, terrazas, palacios y los andenes, luego tendrá
                        tiempo para hacer fotos y explorar por cuenta propia.

                        A la hora oportuna descenderemos de Machu Picchu hacia Aguas Calientes para abordar nuestro tren
                        de retorno a Ollantaytambo y de alli tendremos un tranporte que nos retornara al la ciudad del
                        Cusco.
                    </p>
