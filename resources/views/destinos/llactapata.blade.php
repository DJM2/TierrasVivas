                    <p class="p-salkantay">
                        Un estudio de mediados de 2003 del sitio realizado por Thomson y Ziegler concluyó que
                        la ubicación de Llaqtapata a lo largo del recorrido del Camino Inca de 4 días sugirió que era
                        una importante parada de descanso y un santuario al borde de la carretera en el viaje a Machu
                        Picchu. Esta y otras investigaciones posteriores han revelado un extenso complejo de estructuras
                        y características relacionadas y conectadas con Machu Picchu por una continuación del Camino
                        Inca que conduce hacia Vilcabamba. Llaqtapata pudo haber sido miembro de la red de sitios
                        administrativos y ceremoniales interrelacionados que apoyaron al centro regional en Machu
                        Picchu. Probablemente jugó una función astronómica importante durante los ‘Solsticios’ y
                        ‘Equinoccios’.<br><br>

                        Todos los edificios incluían paredes triangulares, pero solo quedan algunos, ya que la
                        destrucción de las raíces y el crecimiento de los árboles ha causado daños importantes. Dos
                        estructuras en particular contienen paredes divisorias internas muy derruidas. Algunas puertas
                        están parcialmente llenas y un muro de piedra de campo hecho de manera torpe se extiende desde
                        el edificio 2. Estos pueden haber sido agregados más tarde por pastores locales usando el sitio
                        como un cercado.<br><br>
                        <img src="{{ asset('img/galeria/llactapata-inca-trail-to-machu-picchu.jpg') }}"
                            alt="Lago Titicaca Puno" loading="lazy" width="100%"><br><br>

                        Una entrada de doble puerta entre el primer y el segundo edificio indica un alto estatus. Estos
                        se encuentran en las estructuras más importantes en sitios incas regionales como el Coricancha
                        en Cusco, Vitcos, Ollantaytambo y Choquequirao (Gasparini Margolies 1980).

                        Una característica única es un corredor de 145 pies de largo con paredes de seis pies de alto
                        que se alinea con las ruinas de Machu Picchu Perú. La alineación de 65 grados también apunta al
                        amanecer sobre Machu Picchu durante el solsticio de junio. Quedan muchos misterios, ya que este
                        gran sitio ha permanecido mayormente sin estudiar en los últimos años, debido a su relativa
                        inaccesibilidad, excepto por los excursionistas en el Camino Inca.
