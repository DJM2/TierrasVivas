                    <p class="p-salkantay">
                        En 1911, después de descubrir Machu Picchu, Hiram Bingham partió por el Urubamba hacia CHAULLAY,
                        luego por el valle de Vilcabamba hasta el pueblo de Pukyura, donde la evidencia sugirió que
                        podría encontrar las ruinas incas de Vitcos. Atascado en una colina alta con vista a una
                        importante cabecera del río Vilcabamba, Bingham “descubrió” Vitcos, conocido localmente como
                        Rosapata, menos de dos semanas después de encontrar Machu Picchu. Era una ruina relativamente
                        pequeña pero claramente palaciega, basada alrededor de una plaza trapezoidal que se extendía
                        sobre un espolón de cima plana. Abajo de Rosapata, se le mostró a Bingham un manantial que fluía
                        desde debajo de una gran roca de granito blanco intrincadamente tallada en estilo típico inca y
                        rodeada por los restos de un impresionante templo inca. Esta roca blanca “Huaca – Chuquipalta” –
                        15 m de largo y 8 m de alto, fue la prueba de que realmente había encontrado a Vitcos. Era
                        claramente el gran oráculo inca donde los sacrificios de sangre y otros rituales “paganos”
                        habían enfurecido tanto, según las crónicas, a dos sacerdotes españoles (invitados al refugio de
                        los rebeldes incas) que exorcizaron la roca y prendieron fuego a su templo santuario.
                    </p><br>
