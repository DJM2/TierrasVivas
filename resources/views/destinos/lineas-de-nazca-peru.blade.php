                    <p class="p-salkantay">
                        Puedes ver estas líneas desde un observador (mirador) o desde el aire, volando en una avioneta.
                        Esta última es la mejor opción para ver los diseños, como el colibrí, la araña y el mono. Maria
                        Reiche, una investigadora alemana que pasó toda su vida estudiando estas figuras, dijo que estas
                        líneas, de 15 a 20 cm de profundidad, eran un enorme calendario astronómico utilizado para
                        rastrear las diferentes fases solares. También dijo que la tiza de las llanuras del desierto es
                        un preservador mágico y natural de estas imágenes. es absolutamente muy difícil descifrar el
