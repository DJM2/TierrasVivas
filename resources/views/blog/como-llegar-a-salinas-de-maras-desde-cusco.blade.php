                        A unos 6 km (4 millas) por la carretera principal hacia Ollantaytambo (Noroeste de Urubamba) se
                        encuentra la asombrosa vista de las Salineras de Maras, miles de antiguas pozos individuales
                        que se encuentran en terrazas únicas en una ladera. Las minas, pequeñas piscinas densamente
                        recubiertas de sal cristalizada como nieve sucia, han existido en los mismos lugares desde la
                        época de los incas y aún funcionan. Las familias las transmiten como escrituras y continúan con
                        la tradición extenuante y mal remunerada de la extracción de sal (cristalización de sal a partir
                        de agua subterránea de manantial).<br><br>

                        Las famosas salinas incas en terrazas de Salineras todavía están en uso y también aprovechan un
                        fenómeno natural: los incas excavaron piscinas poco profundas en una ladera inclinada. Las
                        piscinas se llenaron de agua y, al evaporarse, la sal se cristalizó y se pudo recolectar. Maras
                        es un llamativo complejo de extracción de sal. Los lugareños aún eliminan la sal de las sartenes
                        de la misma manera que lo han hecho durante miles de años. Ninguno de los dos sitios mantiene
                        horarios de apertura ni cobra entrada, pero ¿cómo llegar a salinas de maras desde cusco? Son
                        difíciles de alcanzar, y casi imposibles durante la temporada de lluvias. Se puede acceder a él
                        a través de un camino de herradura. La forma más fácil es en taxi o transporte público desde
                        Chinchero hasta el pueblo de Maras. También puede caminar a Maras desde Urubamba, que toma
                        varias horas. Pregunte a los lugareños cuál es la mejor ruta. Y otra forma más es a caballo o en
                        bicicleta de montaña desde Cusco o Urubamba.
