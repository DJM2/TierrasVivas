                    <h2>El origen de su nombre histórico</h2>
                    <p class="p-salkantay">
                        El nombre de este sitio arqueológico tiene ligeras variaciones del quechua. Se dice que
                        Chachabamba proviene de 2 palabras “Chacha” que significa verdugones y / o picaduras producidas
                        por los mosquitos que viven en el valle y “Bamba” que no tendría otro significado que valle o
                        lugar, que tendría el resultado final “ Picaduras de mosquitos del valle “.<br><br>

                        Otra de las teorías sobre su posible traducción y que quizás te resulte más bonita de escuchar
                        es: “Valle del origen de las aguas”, que deriva del quechua que obtiene sus palabras de los
                        sonidos onomatopéyicos de la naturaleza, en este caso el Las sílabas Cha … cha … “vendrían del
                        sonido que hace el agua al caer por cascadas” dando pleno sentido a su traducción al español.
                    </p>
