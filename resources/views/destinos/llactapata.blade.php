                    <h3>Camino de Llactapata a Machu Picchu</h3>
                    <p class="p-salkantay">
                        En el idioma quechua, Llaqta significa lugar (aldea, pueblo, ciudad, país, nación), pata
                        significa elevado colocado sobre una orilla (de un río), orilla, se pronuncia ‘yakta-pahta’, es
                        un sitio arqueológico de unos 5 km (3,1 mi) al oeste de Machu Picchu.

                        El complejo está ubicado en el distrito de Santa Teresa de la Región Cusco, en lo alto de una
                        cresta entre los drenajes de Ahobamba y Santa Teresa. Parece ser el sitio reportado
                        originalmente por Hiram Bingham con el mismo nombre. Aunque el sitio fue poco explorado por
                        Bingham, fue explorado y mapeado más extensamente por la expedición de Thomson y Ziegler de
                        2003.
                    </p>
