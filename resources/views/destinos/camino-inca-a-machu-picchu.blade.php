                    El primer punto de la caminata es Huayllabamba, siguiendo el río Cusichaca, es de
                    aproximadamente 3 horas y no es demasiado arduo. Más allá de Huayllabamba, un deporte de
                    campamento popular para grupos de turistas, hay lugares para acampar aproximadamente una hora
                    antes, en Llulluchayoc (3200m). Una penosa subida de una hora y media más es Llulluchapampa, un
                    prado ideal para acampar. Si tienes la energía para llegar a este punto, el segundo día será más
                    fácil porque la siguiente etapa, el ascenso al primer paso, Warmiwañuska (Paso de Mujeres
                    Muertas) a 4200 m, es absolutamente agotador 2 1/2 hrs.<br><br>

                    Luego tome el empinado camino cuesta abajo hacia el valle de Pacaymayo, tenga cuidado de dormir
                    en los lluviosos escalones incas. Puede acampar junto a un arroyo en la parte inferior (1 1/2
                    horas desde el primer paso). Ya no está permitido acampar en Runkuracay, en el camino hasta el
                    segundo paso (una subida mucho más fácil de 3850 m). Magníficas vistas cerca de la cumbre cuando
                    hace buen tiempo. Un buen lugar para pasar la noche está a unos 30 minutos después de las ruinas
                    incas de Sayacmarca (3500 m), aproximadamente una hora después de la cima del segundo paso.<br><br>

                    Luego, una suave subida de 2 horas por una fina carretera de piedra conduce a través de un túnel
                    Inca hasta el tercer paso. Cerca de la cima hay una vista espectacular de toda la cordillera de
                    Vilcabamba. Se desciende a las ruinas incas de Phuyupatamarca (3650 m), bien merece una visita
                    larga, incluso acampando durante la noche. Aquí hay un ‘baño turístico’, donde se puede
                    recolectar agua (pero purificarla antes de beber). Desde allí, los escalones descienden hasta
                    las impresionantes ruinas de Wiñay Wayna (2700 m) con vistas a las terrazas recientemente
                    despejadas de Intipata. El acceso es posible, pero el sendero no es fácilmente visible. Hay un
                    pequeño camping.<br><br>

                    El camino desde este punto va más o menos llano a través de la selva hasta la empinada escalera
                    que sube al Intipunku (2 hrs), donde hay una magnífica vista de Machu Picchu, especialmente al
                    amanecer, con el sol entrando y saliendo alternativamente, las nubes a veces oscurecen el
                    ruinas, a veces dejándolas despejadas.
