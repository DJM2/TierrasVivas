                    <h2>Historia de las ruinas de Patallacta</h2>
                    <p class="p-salkantay">
                        Patallaqta fue quemada por Manco Inca Yupanqui, quien destruyó varios asentamientos a lo largo
                        del sistema de caminos incas durante su retirada de Cusco en 1536 para desalentar la persecución
                        española. En parte debido a estos esfuerzos, los españoles nunca descubrieron el Tour Camino
                        Inca de 4 días a Machu Picchu o cualquiera de sus asentamientos.

                        Se cree que un Inca de alto rango había ordenado la construcción de Llactapata y que el lugar
                        debió haber tenido alguna relación con Machu Picchu (ubicado más al noroeste).

                        Este complejo arqueológico fue descubierto por Hiram Bingham en 1912, poco después de haber
                        llegado por el majestuoso Machupicchu.<br><br>

                        Bingham tuvo muchos problemas para descubrir los densos complejos de vegetación. El cercano
                        valle de Acobamba Llactapata también estaba infestado de serpientes venenosas y la altura de los
                        árboles también impidió observaciones claras. La altura de los árboles también impidió
                        observaciones claras, a pesar de que el equipo había realizado enormes esfuerzos para cortar la
                        vegetación de abajo con machetes para poder pasar.

                        Poco después de ser encontradas, las ruinas de Patallacta fueron nuevamente cubiertas por la
                        vegetación. Este sitio fue examinado a fondo en el 2003 por la expedición de Ziegler-Thomson.
                        Llegando a la conclusión de Patallacta fue un lugar muy importante cerca del Camino Inca que
                        conduce a Machupicchu probablemente fue un santuario.
