                    <h2>El Tiempo en Phuyupatamarca</h2>
                    <p class="p-salkantay">
                        El clima en Cusco y el Camping Phuyupatamarca es semi-tropical durante todo el año, con días
                        soleados y noches muy frías. Como se mencionó anteriormente, es mejor evitar las temporadas de
                        lluvias y separar la plaza en la ruta con meses de anticipación (preferiblemente entre mayo y
                        octubre). Las temperaturas máximas (entre 20 ° C y 26 ° C) y temperaturas mínimas (entre 0 ° C y
                        11 ° C) se registran en junio.<br><br>

                        Debes llevar ropa ligera y calzado de montaña. También es imprescindible disponer de prendas
                        impermeables, forros polares, cortavientos e implementos de protección para el frío o la lluvia
                        de noche.
                    </p>
                    <h2>Phuyupatamarca en el Camino Inca Clásico de 4 días</h2>
                    <p class="p-salkantay">
                        Durante el día 3 marchamos desde el sitio arqueológico de Sayacmarca, a través de una vista
                        inolvidable que incluye bosques con hermosas plantas, aves exóticas y el sinuoso río Urubamba,
                        llegamos al campamento Phuyupatamarca . Ya estamos en el límite occidental de la selva amazónica
                        peruana. En “la ciudad de las nubes” tendrá una visita con un guía profesional nativo, podrá
                        tomar extraordinarias fotos, videos, conocer más sobre el imperio Inca y seguir disfrutando de
                        la inigualable energía natural de los Andes cusqueños.<br><br>

                        Desde allí, el Tour Camino Inca de 4 días se dirige hacia el fabuloso sitio Inca de Wiñaywayna,
                        una obra maestra de ingeniería dedicada a aumentar la producción agrícola, la base de toda su
                        economía. Las terrazas de cultivo escalonadas crean una vista que lo dejará completamente
                        impresionado. Wiñaywayna es también el lugar del campamento y pernocta el día 3.<br><br>

                        El próximo y último día, ya muy lejos del Camping Phuyupatamarca , partimos hacia el fabuloso
                        Inti Punku o Cubierta del Sol, que fue la entrada a Machu Picchu durante la época del imperio.
                        Desde allí se tiene una vista clásica de la ciudadela, ideal para excelentes fotografías.
                        Inmediatamente después, la expedición continúa hacia el sitio de Machu Picchu, para el tan
                        esperado recorrido con un guía profesional. Después de esto, tendrá tiempo extra para continuar
                        explorando el lugar, que fue construido como un suntuoso lugar de vacaciones religiosas para la
                        nobleza Inca, también rodeado de nubes blancas.<br><br>

                        Finalmente, por la tarde se puede disfrutar del agradable Aguas Calientes, una pequeña ciudad a
                        los pies del santuario Inca, en medio de la hermosa selva subtropical. Allí encontrarás comida
                        típica, bebidas, jugos exóticos de primera, masajes exclusivos y terapias de belleza con
                        productos andinos, el enorme y surtido mercado artesanal y por supuesto el relajante complejo de
                        aguas termales que le da nombre al pueblo, entre otros. atracciones. Durante los días 3 y 4 del
                        Camino Inca Clásico habrás caminado 21 kilómetros en la majestuosa geografía andina.
                    </p>
                    <h2>¿Cómo llegar a las ruinas de Phuyupatamarca?</h2>
                    <p class="p-salkantay">
                        El costo del pasaje de bus Lima-Cusco varía entre S /. 180 y S /. 250 soles aproximadamente,
                        según la empresa de transporte o la época del año. La duración del viaje es de 22 horas en
                        promedio, por lo que le recomendamos que reserve al menos tres o cuatro días para completar su
                        recorrido.<br><br>

                        Las Ruinas de Phuyupatamarca son parte de los atractivos del tour Camino Inca a Machu Picchu.
                        Esto es muy solicitado por turistas de todo el mundo; por lo tanto, deberá reservar un lugar con
                        al menos seis meses de anticipación, ya que las autoridades solo permiten el ingreso de 500
                        personas por día (incluye chefs, guías turísticos, porteadores, etc.).<br><br>

                        Para llegar a este hermoso atractivo arqueológico del Cusco, el visitante debe optar por seguir
                        el Camino Inca (Camino Inca Clásico, Caminata de 3 días a Machu Picchu , Camino Inca de 2 días ,
                        Camino Inca de 2 días con campamento , Camino Inca de un día ) que conduce a Machu
                        Picchu.<br><br>

                        Te recomendamos reservar este lugar entre los meses de mayo y octubre. Esta temporada se
                        caracteriza por la menor cantidad de lluvia y el mayor nivel de vegetación. Esto te facilitará
                        la ruta del trekking y, además, disfrutarás mejor de los paisajes.
                    </p>
