                    <p class="p-salkantay">
                        Puno es una de las ciudades más importantes de la orilla del lago Titicaca, el lago más
                        navegable y más alto del mundo. La ciudad de Puno es considerada la Capital Folklórica de
                        América, por su variedad de 300 bailes tradicionales. Los “Caporales” y la “Diablada” son bailes
                        típicos que se realizan durante fiestas como la de la “Virgen de la Candelaria”, patrona de la
                        ciudad. Esta festividad se celebra en febrero y reúne a miles de turistas y lugareños.<br><br>
                        <img src="{{ asset('img/galeria/lago-titicaca.webp') }}" alt="Lago Titicaca Puno"
                            loading="lazy" width="100%">
                        <br><br>

                        Puno se encuentra en estas frías llanuras altas a 4000 m. sobre el nivel del mar. La ciudad
                        también conocida como la tierra de los camélidos, fue el hogar de la cultura Tiahuanaco hace
                        muchos miles de años. Esta cultura tiene algunos de los legados más bellos como la Puerta del
                        Sol, un templo esculpido en piedra.

                        Puno es un sitio turístico importante cerca del lago Titicaca. La mayoría de sus edificios
                        fueron de adobe y los techos están cubiertos con paneles de metal. El centro de la ciudad es
                        pequeño. Si te gusta la vida nocturna ir a Jirón Lima es una necesidad, puedes encontrar allí
                        diferentes restaurantes y lugares a donde ir si quieres escuchar buena música. Allí también se
                        encuentra su Plaza de Armas que fue construida en 1747 por Simeón de Asta.<br><br>

                        Durante la época de los españoles, Puno se hizo muy popular por sus minas Laykakota. Siglos
                        después, Puno junto con Arequipa se convirtieron en los principales centros textiles de fibras
                        de Alpaca. Muchos lugares interesantes rodean el lago y la ciudad. Uno de ellos es Sillustani.
                        Este sitio arqueológico se encuentra aproximadamente a 30 minutos de Puno, cruzando la laguna
                        Umayo y es famoso por sus Chullpas, torres de piedra con forma de cono grande. Chucuito y Pomata
                        son ciudades que rodean el lado sur del lago y son un interesante circuito para admirar
                        magníficas iglesias de estilo renacentista-barroco. De camino al norte se encuentra Juliaca, la
                        única ciudad con aeropuerto y la más importante de la región.
