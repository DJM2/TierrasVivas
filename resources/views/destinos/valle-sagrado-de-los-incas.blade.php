                    <h3>Complejos Inca en el Valle Sagrado:</h3>
                    <p class="p-salkantay">
                    <ul>
                        <li>
                            <strong>Pisaq - 2,715 m.s.n.m.</strong><br>
                            Se encuentra a 33 km al noroeste de Cusco. Allí se pueden encontrar dos atractivos: la
                            ciudadela inca y el pueblo colonial con su tradicional mercado de artesanías. La auténtica
                            ciudad inca está asentada en una montaña escarpada sobre la actual ciudad de Pisac. Esta
                            impresionante ciudadela se extiende a 4 kilómetros. La ciudadela incluye un centro
                            ceremonial (espiritual) con Inti Watana (Reloj Solar), templos que muestran mampostería de
                            piedra perfecta, varios canales de agua, baños ceremoniales, zona militar, zona residencial
                            y montones de tumbas incas colocadas en agujeros en un acantilado. Toda la ciudadela está
                            rodeada de terrazas agrícolas ubicadas en pendientes pronunciadas.
                        </li>
                        <li>
                            <strong>Urubamba - 2.870 m.s.n.m.</strong><br>
                            Es una ciudad hermosa y una capital no oficial del Valle Sagrado. Urubamba abunda en un
                            clima agradable que es el más cálido de todo el valle. La ciudad también es rica en muchos
                            restaurantes turísticos encantadores que ofrecen almuerzos buffet que consisten en cocina
                            típica andina.
                        </li>
                        <li>
                            <strong>Ollantaytambo - 2,000 m.s.n.m.</strong><br>
                            ¡Es el último pueblo inca aún vivo! La ciudad ha estado poblada continuamente durante más de
                            700 años. Sus calles están pavimentadas con adoquines y puedes ver muchos canales de agua
                            auténticos por toda la ciudad. Lo más destacado de la ciudad es su complejo inca que alguna
                            vez sirvió como templo y como fortaleza. El complejo está rodeado por escarpadas terrazas
                            que también tenían un propósito protector. En la parte superior del complejo se encuentra un
                            área ceremonial, que representa un asombroso ejemplo de la arquitectura Inca. El área
                            ceremonial consta de tres piedras realmente enormes que encajan perfectamente entre sí. El
                            complejo fue testigo de una de las batallas más importantes cuando los conquistadores
                            españoles fueron derrotados por el gobernante inca Manco Inca.
                        </li>
                        <li>
                            <strong>Chinchero - 3,160 m.s.n.m.</strong><br>
                            Es un pequeño pueblo a solo 28 km de Cusco. Está rodeado por los picos nevados de Salkantay,
                            Soray y Veronica. Allí se encuentran las ruinas del palacio de Tupac Yupanqui (un gobernante
                            inca) destruido por los españoles en 1607 para que se construyera una iglesia colonial
                            llamada “Iglesia de Nuestra Señora de Monserrat” en su parte superior con el fin de
                            demostrar su dominio. Todo este sitio está rodeado por muchas terrazas agrícolas incas.
                            Chinchero también puede enorgullecerse de hermosos y finos tejidos con diseños típicos del
                            sitio. Los textiles y otras artesanías se pueden encontrar en su mercado típico diario.
                        </li>
                        <li>
                            <strong>Moray - 3500 m.s.n.m.</strong><br>
                            Es un sitio arqueológico a 50 kilómetros al noroeste de Cuzco ya poca distancia al oeste del
                            pueblo de Maras. Este es un sitio inca muy extraordinario que consta de muchas terrazas
                            enormes formadas por depresiones circulares. El mayor de ellos tiene unos 30 metros de
                            profundidad. El propósito de estas depresiones era crear diferentes temperaturas que
                            permitieran a los incas estudiar los efectos de las diferentes condiciones climáticas en los
                            cultivos y luego, su cultivo. Por esa razón, Moray es considerado un sitio experimental
                            agrícola inca, así como un prototipo del primer invernadero. Moray también tiene un
                            complicado sistema de riego.
                        </li>
                        <img src="{{ asset('img/galeria/sacred-valley-of-the-incas-tour.jpg') }}"
                            alt="Lago Titicaca Puno" loading="lazy" width="100%"><br><br>
                        <li>
                            <strong>Salineras - 3200 m.s.n.m.</strong><br>
                            Son minas de sal que se han utilizado desde tiempos preincaicos. La sal proviene de una
                            corriente de minerales salados subterráneos que brota en una colina sobre las minas de sal.
                            El flujo del arroyo desemboca en un complicado sistema de diminutos canales que riegan una
                            gran cantidad de pequeñas piscinas saladas. Hay unas 3.000 piscinas saladas. Aparte de eso,
                            las minas de sal ofrecen una vista abrumadora cuando se ve desde arriba el camino de acceso.
                        </li>
                    </ul>
                    </p><br><br>


