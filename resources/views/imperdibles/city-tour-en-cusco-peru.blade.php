                    <h2>Itinerario city tour Cusco:</h2>
                    <p class="p-salkantay">
                        Los recogeremos de antes del inicio del city tour, para después iniciar nuestra visita a los
                        principales atractivos turisticos de la ciudad del Cusco. <br><br>

                        Nuestro primer punto a visitar sera el Templo de Qoricancha (en quechua, Recinto de Oro) este
                        maravilloso templo es considerado sagrado ya que albergaba a sacerdotes Incas encargados de
                        adorar al Dios Sol. En la época de los incas se llamó la “casa de sol” y fue un centro religioso
                        de gran importancia. Luego visitaremos Sacsayhuaman, nombre quechua que significa “halcón
                        satisfecho” fue construido con enormes bloques de piedra. Luego visitaremos Qenqo palabra
                        quechua que significa “Laberinto” acuñado por los españoles por sus laberínticas galerías
                        subterráneas y por los pequeños canales labrados en zigzag.<br><br>

                        Luego conoceremos Puca Pucara: proveniente de dos palabras quechua que significa Puca (rojo)
                        Pucara (fortaleza) “fortaleza roja”, lugar estratégico para control y vigilancia de acceso y
                        salida de la ciudad Imperial del Cusco, el cual se usaba como frontera territorial para dividir
                        las regiones y establecer los regímenes de desplazamiento.<br><br>

                        Finalmente visitaremos Tambomachay: lugar de descanso, ubicado a 9 km de la ciudad del Cusco,
                        más conocida como los Baños del Inca por las fuentes de agua, o también conocida como las aguas
                        de la eterna juventud por el caudal de sus aguas que es constante y nos ofrece parte del camino
                        inca que va de Cusco hacia Machupicchu. Este city tour en la ciudad del Cusco finaliza a las
                        18:30 hrs aproximadamente.
                    </p>
                    <h2>Qoricancha:</h2>
                    <p class="p-salkantay">
                        El convento fue construido sobre el espectacular Koricancha o sitio de oro, el más importante
                        dedicado al culto del sol, cuyos muros estaban recubiertos por láminas de oro. Este convento se
                        levanta empleando como base estructuras de piedra pulida finamente trabajadas. Su portón es una
                        excelente muestra del renacimiento y su única torre de estilo barroco, destacada sobre los
                        techos de tejas de la ciudad. Como todos los templos, cuenta con una importante colección de
                        lienzos de la escuela cuzqueña.
                    </p>
                    <h2>Sacsayhuaman:</h2>
                    <p class="p-salkantay">
                        Imponente ejemplo de arquitectura militar incaica, esta fortaleza fue construida con grandes
                        bloques de granito y dedicada al resguardo de la ciudad del Cusco de los ataques de los
                        invasores del oriente o Antis. Sacsayhuaman (en quechua halcón satisfecho) está formada por tres
                        grandes terrazas superpuestas en forma de “zigzag” y bordeado por enormes murallas de roca de
                        hasta 300 mt. De longitud. Por su cercanía y elevación con respecto al Cusco y por las
                        dimensiones de sus piedras de hasta 5 mt. De altura y 350 toneladas de peso, este sirvió de
                        cantera para ciertas edificaciones del Cusco colonial.
                    </p>
                    <img src="{{ asset('img/galeria/City-Tour-en-Sacsayhuaman-Cusco-Peru.webp') }}"
