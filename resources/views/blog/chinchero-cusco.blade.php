                    <h2>Ubicación de Chinchero</h2>
                    <p class="p-salkantay">
                        El Pueblo de Chinchero en Perú, se encuentra a 28 km. al noroeste de la ciudad del Cusco, a unos
                        3.160 metros sobre el nivel del mar, rodeada por los nevados de Salkantay, Verónica y Soray. La
                        vista desde aquí es impresionante. Chinchero pertenece a la provincia de Urubamba, departamento
                        del Cusco.
                    </p>

                    <h2>Historia de Chinchero en el Valle Sagrado</h2>
                    <p class="p-salkantay">
                        Los orígenes de Chinchero en Cusco - Perú se pierden en la noche de los tiempos. Hay vestigios
                        que datan aproximadamente de dos mil años. Los primeros habitantes de la región fueron los
                        ayarmacas quienes, cuando llegaron los primeros gobernadores cusqueños, defendieron su
                        territorio y ofrecieron una seria resistencia antes de incorporarse al imperio. Chinchero fue el
                        lugar elegido por el Inca Tupac Inca Yupanqui para establecer su residencia. Hizo construir
                        hermosos palacios para su uso personal y el de su panaca. <br><br>

                        Hacia 1536, en medio de la invasión, Manco Inca inició su rebelión quemando Chinchero en Cusco -
                        Perú para que los españoles no renovaran sus provisiones y dejaran de perseguirlo en su retirada
                        a desconocidas regiones selváticas. Cuando el virrey Toledo visitó el Cusco, se detuvo en
                        Chinchero. Aquí estableció una reducción de indios y ordenó la construcción de la iglesia
                        actual, la cual se construyó sobre hermosas habitaciones incas. Posteriormente, durante la
                        revolución de Túpac Amaru II, el curaca de Chinchero, Mateo García Pamacahua, se alzó a favor
                        del Rey de España para combatir al rebelde. El triunfo de Pumacahua quedó eternizado en un mural
                        que hoy presenta a un puma derrotando a una serpiente (amaru).<br><br>

                        Oficialmente, el distrito de Chinchero en Cusco - Perú fue creado el 9 de septiembre de 1905
                        mediante la Ley No. 59 dada en el gobierno del Presidente José Pardo y Barreda.

                        He aquí lo que en la época inca fue la hacienda real de Tupac Inca Yupanqui, así como un templo
                        colonial construido sobre los cimientos de esa civilización, sobre todo las típicas puertas o
                        ventanas más anchas abajo y más estrechas arriba, que era una característica de la arquitectura
                        inca .
                    </p>
