                    <p class="p-salkantay">
                        Si llega sin boleto de admisión, debe adquirir uno en Machu Picchu en el Ministerio de Cultura
                        (dirección: Avenida Pachacutec s / n, teléfono: 084-211196). Si viene con un tour a Machu
                        Picchu, lo más probable es que se encarguen de las entradas.<br><br>

                        Compre su boleto la noche anterior si desea ingresar al santuario histórico de inmediato (el
                        servicio de autobús comienza a las 5:30 a.m.). Desde el momento en que compra el boleto, tiene
                        solo un dia para ingresar a la ciudadela Inca. Sin embargo, una vez que ingresas a la ciudadela,
                        el boleto solo es válido para ese día. Entonces, si llega por la tarde y visita las ruinas y
                        luego pasa la noche con la intención de regresar a la ciudadela a la mañana siguiente, tendrá
                        que comprar dos boletos (uno para cada día).
