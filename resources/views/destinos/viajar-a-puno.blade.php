                    En la Avenida Torre se puede ver una vía férrea y un interesante mercado de frutas. La gente en
                    Puno anda en bicicleta y es el medio de transporte más famoso, incluso las usa como taxis. Todos
                    los visitantes van al puerto y a la avenida Titicaca, pues se ofrecen paseos en bote junto con
                    un guía turístico para visitar las islas flotantes de Uros, Taquile y Amantani. La isla de
                    Taquile se encuentra a 35 kilómetros de Puno, fue utilizada como prisión política en el siglo
                    XX. La isla Amantaní está ubicada a 36 kilómetros del puerto de la ciudad de Puno. Esta isla
                    está
                    llena de arbustos. Para turistas y lugareños es muy interesante visitar los miradores naturales,
                    desde allí puedes tener la mejor experiencia de tu vida. Desde estos miradores se pueden ver los
                    templos prehispánicos de Pacha Tata, de Pacha Mama, Incachincana, Inca Tihana y el cementerio de
                    las momias. <br><br>

                    Sitio arqueológico de Cutimbo, ubicado en el kilómetro 22 de la carretera Puno y Moquegua, se
                    encuentra un cementerio prehispánico que perteneció a los lupacas y collas. Hay señales que
                    indican que los incas vivieron allí durante la época del Imperio. El sitio arqueológico de
                    Sillustani, ubicado a 34 kilómetros de Puno es famoso por sus
                    “chullpas”, torres redondas levantadas para albergar los restos de sus principales autoridades
                    del pueblo del Collao. A corta distancia se puede ver su museo donde se encuentran diferentes
                    piezas arqueológicas de diferentes culturas como Colla, Tihuanaco e Inca.<br><br>

                    Los Uros son un grupo de personas que viven en “islas flotantes” que han hecho artificialmente
                    con cañas de totora, y navegan en sus tradicionales embarcaciones también hechas con cañas de
                    totora. Taquile, Suasi y Amantaní son conocidos por la amabilidad de sus habitantes, su
                    habilidad ancestral en el tejido, sus construcciones precolombinas y su hermoso paisaje. La
                    Reserva Nacional Titicaca (36.180 hectáreas) protege extensos tramos de totora y diversas
                    especies de plantas y animales. Disfruta de los bailes, lagos y hermosos paisajes de Puno, y no
                    olvides incluir en tu itinerario el pueblo de Lampa y Azangar, cuyos templos guardan tesoros por
                    descubrir.
