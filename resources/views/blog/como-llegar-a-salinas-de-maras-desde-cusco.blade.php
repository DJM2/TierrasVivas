                    <h2>Cómo llegar a Salinas de Maras desde Cusco</h2>
                    <p class="p-salkantay">
                        Muchos turistas preguntan cómo llegar a salinas de Maras desde Cusco. En realidad es muy fácil,
                        para llegar a las salinas se toma un taxi (USD$2) hasta un punto cercano al pueblo de Tarabamba;
                        puede hacer que el taxi lo espere o llamar a una combi en la carretera principal para su
                        regreso. A partir de ahí, es una hermosa caminata de 4 km (2 millas) bajo un cielo enorme y por
                        un sendero junto al río. No hay señales; cruce el puente peatonal y gire a la derecha a lo largo
                        del otro lado del río y suba a través de las montañas hacia las salinas. A medida que comience
                        el suave ascenso de la montaña, manténgase en el camino correcto para evitar el sendero que
                        abraza el acantilado y solo unas pulgadas de ancho que se bifurca a la izquierda..
                    </p>

