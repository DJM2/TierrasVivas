                    <h3>Historia de Runkurakay</h3>
                    <p class="p-salkantay">
                        Runkuraqay o Runku Raqay (canasta runku en quechua, cobertizo raqay / casa abandonada / ruina)
                        Es un sitio arqueológico en una montaña del mismo nombre en Perú ubicado en la región de Cusco,
                        provincia de Urubamba y distrito de Machu Picchu.<br><br>

                        Está situado al sureste del sitio arqueológico Machu Picchu y al sur del río Vilcanota.
                        Las ruinas se encuentran en la ladera sur de la montaña Runkuraqay cerca del paso Runkurakay ,
                        al noreste del sitio arqueológico Sayacmarca y al sureste del sitio Qunchamarka.<br><br>

                        Según la historia de Runkurakay, Hiram Bingham III visitó el sitio en abril de 1915. Luego, la
                        historia de Runkurakay dice que Paul Fejos lo visitó en 1940.
                    </p>
                    <h3>Turismo en Ruinas de Runkurakay</h3>
