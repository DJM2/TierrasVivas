                    <h2>Calendario Solar Inca</h2>
                    <p class="p-salkantay">
                        El Calendario Solar Inca se determinó a partir de la observación del Sol y la Luna. Alrededor de
                        la ciudad del Cusco había doce pilares dispuestos de tal manera que en cada mes uno
                        de ellos indicaba dónde salía el sol y dónde se ponía. Estos pilares se llamaron sucanga; y con
                        ellos se anunciaron las fiestas y tiempos de siembra y recolección. El año de 360 ??días se
                        dividió en 12 lunas de 30 días cada una. Cada luna correspondía a festividades y actividades
                        diarias como las siguientes:
                    </p>
                    <h3>Adoración al Sol Inca</h3>
                    <p class="p-salkantay">
                        El Culto al Sol Inca comienza antes del amanecer, cuando el Inca, su familia y el pueblo se
                        dirigieron en solemne procesión hacia la plaza principal del Cusco donde esperaron en silencio
                        el sol naciente, cuya aparición fue recibida con júbilo. Todos los presentes se arrodillaron
                        entonces y el Inca ofrece la bebida sagrada al Dios Sol
                        Inca, la “chicha” en un cuenco de plata.

                        Luego marchan hacia el Coricancha, donde se reavivó el fuego sagrado mediante el uso de espejos
                        de Oro.

                        El Culto Inca al Sol se acompañó con danzas y sacrificios de granos, flores y animales, los
                        cuales fueron quemados en hogueras.
                    </p>
                    <h3>Fiesta del Sol Inca</h3>
                    <p class="p-salkantay">
                        También hubo un festival especial que los Incas celebraban en honor al Dios Sol Inca. Se le
                        conocía como Inti Raymi, y se celebraba con motivo del solsticio de invierno, en el mes de
                        Junio.

                        Durante el Festival del Sol Inca, que duraba varios días, se sacrificaron llamas blancas y otros
                        animales en honor al Dios Sol.

                        La Fiesta del Sol Inca todavía se celebra en la actualidad en toda la región andina, en países
                        como Bolivia, Ecuador y Perú.

                        Para una experiencia completa en Cusco se recomienda visitar <a href=""
                            class="enlaces-tours"><strong>Machu Picchu</strong></a> después del Festival
                        del Inti Raymi.
                    </p>
