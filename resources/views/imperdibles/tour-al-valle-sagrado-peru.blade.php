                    <p class="p-salkantay">
                        El Valle Sagrado de los Incas fue sin duda un área clave de asentamiento para los Incas. Su
                        clima agradable y sus llanuras fértiles hacen una combinación rara y fructífera para los altos
                        Andes. También era la ruta a la selva y, por lo tanto, un área con acceso a frutas y plantas de
                        tierras bajas tropicales. El Valle Sagrado también sirvió como zona de amortiguación,
                        protegiendo a Cusco de las invasiones de los Antis, tribus de la selva feroz asaltaron las
                        tierras altas andinas de vez en cuando.<br><br>

                        El tour al Valle Sagrado involucra visitas guiadas a los centros arqueológicos más importantes
                        de la ruta de Urubamba como Pisac, Ollantaytambo y el mirador de Chinchero. Con una breve parada
                        para el almuerzo en el pueblo de Urubamba. Explore y descubra esta ruta Inca.
                    </p>

                    <h2>Itinerario al Valle Sagrado</h2>
                    <p class="p-salkantay">
                        Este tour del Valle Sagrado de los Incas inicia a las 8:30 a. m., los recogemos de su hotel en
                        Cusco 30 minutos antes del inicio de la excursión, empezamos visitando el Centro Arqueológico de
                        Pisac situado a 45 minutos de Cusco en transporte turístico.<br><br>

                        Pisac le dará una idea del avanzado conocimiento que lograron los Incas en la arquitectura. Al
                        igual que en otras construcciones incas, algunos estudios revelan que no se trató de una
                        fortaleza, sino de una especie de “hacienda real”, propiedad del Inca Pachacútec, compuesta de
                        andenes, estructuras domésticas, ceremoniales y acueductos que proporcionaban el agua para la
                        explotación agrícola. Otra buena razón para visitar Pisac es su mercado artesanal, donde se
                        pueden encontrar algunos recuerdos únicos y auténticos.<br><br>

                        Al mediodía nos dirigimos al poblado de Urubamba donde disfrutamos del almuerzo buffet, una
                        muestra de la deliciosa gastronomía peruana. Urubamba fue uno de los principales centros
                        agrícolas del Imperio de los Incas. Actualmente aquí se cosecha el mejor maíz del mundo y en
                        temporada de lluvias abundan las frutas propias del valle, como duraznos, capulí, membrillo,
                        frutillas de primera calidad, entre otros.<br><br>
