                    <h2>Machu Picchu y montaña Huayna Picchu</h2>
                    <p class="p-salkantay">
                        Hay dos picos de montañas que se elevan sobre el sitio arqueológico de Machu Picchu. Para
                        escalar una de las montañas, debe comprar un pase en combinación con su boleto de entrada, no se
                        puede comprar por separado del boleto de entrada, y una vez que se ha comprado el boleto de
                        entrada, no se puede agregar ni cancelar un pase de montaña.<br><br>

                        La subida al Huayna Picchu es otro punto culminante en este mágico lugar. El nombre Huayna
                        Picchu significa “montaña joven”, comparado con Machu Picchu “la montaña vieja”. Desde lo alto
                        de la montaña joven se tiene una vista extraordinaria del complejo arqueológico y del sendero
                        que conduce a las terrazas al borde del abismo así como de algunas pequeñas ruinas de templos.
                        Más allá de los mitos se encuentra el testimonio del saber y la mayor expresión artística de los
                        Incas. Esta ciudad capta la atención de todos sus visitantes que admiran su magnífica belleza.
                    </p>
                    <h2>Recomendaciones para viajar a Machu Picchu</h2>
                    <p class="p-salkantay">
                        Es recomendable viajar en la época seca que es de Abril a Octubre. Si vas en época de lluvias,
                        que es de noviembre a marzo, ten en cuenta las condiciones climáticas que pueden variar. Al
                        llegar a Cusco es importante que al menos descanses unas 2 horas para que tu cuerpo se adapte a
                        la altitud.
                    </p>
