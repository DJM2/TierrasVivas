                                    <p class="p-salkantay">
                                        Lo mas recomendable de viajar al Cusco y Machu Picchu, es planificar los dias de
                                        visita, para tener todo organizado y no peder un dia de exploracion. Puede
                                        descargar un app o anotar en su calendario de su celular.
                                    </p>
                                    <h3>Paso 3: Disponibilidad y Reserva</h3>
                                    <p class="p-salkantay">
                                        Antes de reservar el hotel, vuelo y transporte. Revise la disponibilidad de
                                        espacios a Machu Picchu. Una vez confirmado con su fecha de viaje e itinerario.
                                        Emprenda la reserva de los demas servicio a usar en su viaje. Si planea hacer la
                                        ruta del Camino Inca de 4 o 2 dias, los espacios son limitados.<br><br>

                                        Para el Camino Inca de 4 Dias son 500 espacios disponibles, entre pasajeros,
                                        guias, cocinero y porteadores. Por ejemplo: Para 2 Pasajeros, necesita 1 Guia, 1
                                        Cocinero y 5 Porteadores, en total son 9 espacios disponibles.<br><br>

                                        Para el Camino Inca de 2 Dias, solo son 300 espacios disponibles. Tiene dos
                                        opcions de viaje, el Camino Inca corto con hotel o campamento. En caso de elegir
                                        con hotel, unicamente necesita 3 espacios disponibles. Por ejemplo: 2 Pasajeros
                                        + 1 Guia. Pero en caso de Camino Inca 2 dias con campamento, necesitaria la
                                        misma cantidad anteriormente mencionado.<br><br>

                                        Para hacer la ruta Camino Inca, necesitara de un operador turistico autorizado
                                        por el MInisterio de Cultura y SERNARP.

                                        Lo ideal es reservar con una empresa autorizada al menos 4 o 5 meses antes de su
                                        viaje. Los permisos están disponibles todo el año, pero se agotan, así que
                                        asegúrese de planificar con anticipación. No se otorgan permisos de Camino Inca
                                        para Febrero, Por mantenimiento de la ruta.<br><br>

                                        Existen rutas alternativas al Camino Inca, que son caminos menos transitados.
                                        Consulte con nuestro estilo de viaje Peru Aventuras.
