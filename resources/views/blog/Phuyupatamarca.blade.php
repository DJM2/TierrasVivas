                    <h2>¿Cuánto cuesta llegar a las ruinas de Phuyupatamarca?</h2>
                    <h3>Costo en bus</h3>
                    <p class="p-salkantay">
                        El precio promedio de las agencias de turismo por la ruta tradicional de cuatro días varía entre
                        S / .600 y S / .1800 dólares. Este monto depende del paquete turístico contratado, algunos
                        ofrecen servicios de chef en el camino, suministros de campamento, etc.
                    </p>
                    <h3>Recomendaciones adicionales</h3>
                    <p class="p-salkantay">
                        Lo que recordarás de este viaje serán los paisajes, la historia que contemplas y los restos
                        arqueológicos. Entonces, si tu presupuesto es limitado, no te preocupes y contrata el paquete
                        que más te convenga. Por supuesto, es mejor que traigas tus propios implementos de campamento
                        (carpa, dormir y más) y tus almuerzos ligeros. Si este es el caso, tu resistencia física debería
                        ser aún mayor ya que tendrás más peso para llevar y no contarás con los servicios de un cargador
                        (algo que incluye paquetes más costosos).<br><br>

                        Como consejos adicionales, beba suficiente agua para hidratarse durante la caminata. Sin
                        embargo, evite tomar grandes cantidades de un solo sorbo, ya que complicará su digestión. Tome
                        solo una o dos bebidas cada quince minutos. Evite las grasas, el tabaco y las actividades muy
                        intensas antes de iniciar la ruta. No olvides llevar baterías extra o cargadores inalámbricos
                        para tu celular o tu cámara, no querrás quedarte sin tomar las mejores fotos.
                    </p>
                    <h2>Consejos para llegar a las ruinas de Phuyupatamarca</h2>
                    <ul>
                        <li>Antes de visitar este lugar en la ruta del Camino Inca, visite la ciudad del Cusco al menos
                            un día antes. Esto te servirá para aclimatarte a la altura mientras descubres todos los
                            misterios, historias, tradiciones y lugares históricos de la ciudad.</li>
                        <li>No se deje engañar por el paquete para ingresar a la ruta del Camino Inca. Actualmente solo
                            existen 200 agencias autorizadas por SERNANP (Servicio Nacional de Áreas Naturales
                            Protegidas por el Estado) para comprarlo. Además, solo podrás ingresar a la ruta en compañía
                            de un guía.</li>
                        <li>Al ser una caminata larga, debes estar en buena condición física para resistir la caminata
                            sin problemas. Recuerde que el desgaste físico, junto con el mal de altura, podría arruinar
                            su aventura. Tómese su tiempo y obedezca siempre las instrucciones y recomendaciones de la
                            guía.</li>
                        <li>Gran parte de la ruta del Camino Inca se ubica entre los 3,500 y 5,000 metros sobre el nivel
                            del mar. La ruta conecta los sectores agrícola, administrativo, minero, religioso, entre
                            otros. En total son 60 mil kilómetros de longitud que nuestros antepasados ??utilizaron para
                            unir todo el Tahuantinsuyo con fines administrativos, políticos y militares.</li>
                        <li>Para recorrer el Camino Inca existe más de un tipo de ruta las cuales varían en la cantidad
                            de días de viaje y destinos a conocer. La ruta tradicional, que tiene una duración de 4
                            días, es la que te permitirá llegar y apreciar los restos arqueológicos de Phuyupatamarca.
                        </li>
                    </ul>
