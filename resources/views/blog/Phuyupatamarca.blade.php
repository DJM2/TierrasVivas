                    <br><br>
                    <p class="p-salkantay">
                        Uno de los destinos más representativos de Cusco es, por supuesto, Machu Picchu. El Camino Inca
                        es también uno de los medios preferidos por los turistas para llegar. En el recorrido es posible
                        observar innumerables paisajes andinos y restos de la cultura Inca, como el mágico
                        Phuyupatamarca. Un destino de ensueño desde el cual te brindamos toda la información
                        necesaria.<br><br>

                        Este lugar es un claro ejemplo de la cultura Inca, su arquitectura, historia y belleza
                        característica. Phuyupatamarca en el Camino Inca tiene una estructura sofisticada de muro de
                        contención y está ubicada en el lado más empinado de una montaña.<br><br>

                        Phuyupatamarca es conocido como “lugar sobre las nubes” por estar ubicado a más de 3600 metros
                        sobre el nivel del mar.
                    </p>
                    <h2>¿Por qué se llama “El lugar en las nubes”?</h2>
                    <p class="p-salkantay">
                        El “lugar sobre las nubes” debe su nombre a que, cuando la niebla de la selva alta se eleva,
                        estar junto a Phuyupatamarca es como flotar sobre las nubes: una experiencia única para
                        cualquier viajero.<br><br>

                        En Phuyupatamarca domina un hermoso paisaje, pues muchos de los que llegaron a este punto camino
                        a Machu Picchu, indican que se sienten como si estuvieran en las nubes, este efecto es causado
                        por la neblina de la selva alta que se eleva hasta el complejo arqueológico. , llenando todo el
                        lugar de una espesa niebla y dando la impresión de estar “flotando” entre las nubes; la
                        experiencia es realmente asombrosa. Visitar este lugar es conocer una antigua ciudadela de
                        nuestros antepasados.
                    </p>

                    <h2>Arquitectura de las ruinas de Phuyupatamarca</h2>
                    <ul>
                        <li>Las Ruinas de Phuyupatamarca tienen quince edificios, dos plazas, seis baños ceremoniales,
                            puentes, escaleras, canales de agua y plataformas de observación – similares a miradores –
                            muy cerca del final; es decir, desde el precipicio. Phuyupatamarca en el Camino Inca es uno
                            de los pocos edificios que todavía están intactos en su mayoría.</li>
                        <li>Hay dos pequeñas plazas, destacan los 6 baños ceremoniales, creando un ambiente clásico y
                            tradicional de filosofía inca y creencias andinas.</li>
                        <li>El sitio arqueológico de Phuyupatamarca está construido sobre un río subterráneo que riega
                            las aguas para sus baños ceremoniales.</li>
                        <li>Hay canales de agua; cuenta con un asombroso sistema hidráulico para regar todas las
                            plataformas, y lo que sorprende a los visitantes es que 500 años después de su construcción
                            sigue en funcionamiento.</li>
                        <li>También podemos mencionar los puentes y escaleras, como en muchos sitios incas, esta no es
                            la excepción que combina el equilibrio entre la naturaleza y el medio ambiente.</li>
                        <li>Destacan las plataformas de observación, desde donde se pueden observar los restos
                            arqueológicos de Intipata y Wiñaywayna.</li>
                        <li>La ciudadela es más grande de lo que parece, ya que es probable que cientos de personas
                            hayan vivido allí hace siglos. Los propósitos de su construcción, para el caso, podrían ser
                            muchos: ceremoniales, estratégicos administrativos, militares, etc.</li>
                    </ul>
