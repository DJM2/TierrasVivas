                        por 1911, por el investigador estadounidense Hiram Bingham, ha habido muchas hipótesis sobre la
                        construcción de la ciudad. Sin embargo, se sabe que los incas construyeron la ciudad en esta
                        accidentada zona de la selva para evitar el acceso y posibles ataques de sus enemigos. Fue el
                        centro inca más importante para el culto y la observación astronómica.<br><br>

                        Tiene dos grandes áreas importantes: al sur, el área agrícola está formada por terrazas,
                        acueductos, etc. Al norte, el área urbana es la ciudad con sus templos, observatorios, talleres
                        y enterramientos sagrados.
                        La forma más impresionante de llegar a la ciudadela es caminando por la ruta conocida como el
                        “Camino Inca” (un viaje de cuarto día). El Camino Inca a Machu Picchu es una de las rutas más
                        espectaculares del mundo, parte de los 23.000 kilómetros de camino que construyeron los Incas
                        por toda Sudamérica.
                    </p>
                    
                    <h2>¿Como llegar a Machu Picchu en tren?</h2>
                    <ul>
                        <li>
                            Todos los turistas que quieren visitar Machu Picchu deben llegar primero a la ciudad de
                            Cusco pues allí está el aeropuerto. Machu Picchu está a más de 100 kilómetros de distancia
                            de la ciudad de Cusco.
                        </li>
                        <li>
                            Para ir de Cusco a Machu Picchu se debe abordar el tren en alguna de las 3 estaciones:
                            Poroy, Valle Sagrado u Ollantaytambo. Esta última es la más concurrida por que cuenta con
                            mayor número de salidas diarias.
                        </li>
                        <li>
                            Cusco está a 79 kilómetros de la estación Ollantaytambo por lo que este tramo se hace en
                            auto (transporte público).
                        </li>
                        <li>
                            En Ollantaytambo se aborda el tren que lo llevará al pueblo de Aguas Calientes (Machu Picchu
                            pueblo).
                        </li>
                        <li>
                            Una vez en Aguas Calientes solo queda abordar uno de los buses que en 30 minutos lo llevará
                            hasta la puerta de ingreso a Machu Picchu.
                        </li>
                        <li>En total todo el trayecto de Cusco a Machu Picchu toma 4 horas.</li>
                    </ul>
