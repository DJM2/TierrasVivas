                    <h3>¿Cuál es la capital de Perú?</h3>
                    <p class="p-salkantay">
                        Una de las teorías sobre el origen del nombre de la ciudad es que proviene de la palabra quechua
                        Limaq. Y su significado es “el que habla” o “el que tiene la capacidad de hablar”. Sin embargo,
                        según el texto, el nombre no se refiere a los habitantes de la ciudad de Lima-Perú, sino al río
                        Rimac al que se le conoce como “el río parlanchín”, porque arrastrar piedras en su cauce genera
                        un sonido similar a un balbuceo.<br><br>

                        La capital de la República del Perú es Lima y fue fundada en 1535. Lima fue fundada por el
                        conquistador español Francisco Pizarro en 1535, quien la eligió como capital por su ubicación
                        estratégica y su principal puerto, el Callao, que fue un punto focal en la “costa de Perú y
                        Sudamérica”. Lima fue la capital política, comercial y eclesiástica española de América del Sur,
                        donde se ubicaba los virreyes de la Santa Inquisición. En la época colonial la ciudad fue
                        considerada la metrópoli más importante de América Latina.<br><br>

                        Hoy, Lima es el centro económico, político del país y el hogar de casi el 30% de la población
                        total del Perú. Sus edificios y templos colombianos y coloniales sobrevivientes dan a la ciudad
                        una maravillosa mezcla de arquitectura. El centro de la ciudad es donde se puede apreciar esta
                        mezcla visitando la Catedral, el Palacio Torre Tagle, San Francisco, Santo Domingo, San Pedro y
                        la iglesia de Las Nazarenas, ademas de las hermosas casas del siglo XIX que tienen
                        impresionantes balcones. Un poco más tarde, camino a Miraflores, otra zona importante de la
                        ciudad, se encuentra el indio Tom Pucllana, un impresionante complejo ceremonial precolombino,
                        que muchos dicen que es un muy buen punto de partida del recorrido.
                    </p>
