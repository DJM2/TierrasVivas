                        comenzaremos nuestro tour guiado por un promedio de dos horas, dentro de la ciudadela de Machu
                        Picchu. Luego usted tendrá tiempo libre para recorrer esta impresionante ciudad inca. Finalmente
                        terminamos el día retornando en tren hacia Ollantaytambo y luego nuestro auto con dirección a
                        Cusco.
