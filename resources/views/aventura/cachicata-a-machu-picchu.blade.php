                    <h3>Día 03: Queñacancha – Camino Inca Quarry – Cachicata – Aguas Calientes</h3>
                    <p class="p-salkantay">
                        Caminamos cuesta abajo siguiendo el sendero hasta que nos unamos al canal de agua Inca que
                        proviene del lago Maracocha, desde aquí llegamos al templo llamado Intiwatana, que se encuentra
                        en el borde de la montaña llamada Apu Alambre, frente al pico de nieve Veronica. Luego
                        continuamos cuesta abajo hasta Choquetacarpo, un sitio arqueológico, que probablemente fue la
                        residencia de arquitectos que administraron las canteras incas y realizaron ceremonias en
                        Intipunku, después del almuerzo nos dirigimos hacia abajo, explorando diferentes sectores de una
                        de las canteras incas más grandes, donde bloques tallados de riolita rosa se encuentran
                        dispersos en desorden. Las piedras extraídas, testimonio de un gran proyecto de construcción que
                        nunca se terminó y se destinaron al sitio inca de Ollantaytambo. Descendemos a la estación de
                        tren y tomamos el tren al pueblo de Aguas Calientes.
                    </p>
                    <h3>Día 04: Machu Picchu – Cusco</h3>
                    <p class="p-salkantay">
                        Tenemos un comienzo temprano en la mañana (antes del amanecer), por lo que podemos llegar a
                        Machu Picchu a través de la Puerta del Sol o Intipunku antes de las multitudes. Caminamos las
                        últimas 2 horas hasta la misteriosa ciudad de Machu Picchu. Visita guiada de Machu Picchu y
                        luego puede explorar este asombroso y misterioso asentamiento inca solo. En la noche tomamos el
                        tren a Ollantaytambo y lo trasladamos a su hotel en Cusco.
                    </p>
