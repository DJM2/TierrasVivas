                    <h3>Tour en el Valle sagrado de los Incas</h3>
                    <p class="p-salkantay">
                        El Valle Sagrado de los Incas es un valle del río Urubamba. El valle comienza a 15 km al norte
                        de la ciudad de Cusco y se extiende a unos 100 km entre dos importantes ciudades incas: la
                        ciudad de Cusco y la ciudadela de Machu Picchu. El lugar tenía un significado especial para los
                        incas ya que el río sagrado de Urubamba representaba un reflejo de la Vía Láctea cósmica en su
                        mitología.
                    </p>
