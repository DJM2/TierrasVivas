                    <h2>Geografía de Chinchero en el Valle Sagrado</h2>
                    <p class="p-salkantay">
                        La capital es el Pueblo de Chinchero en Perú, ubicado a 3.754 metros sobre el nivel del mar. A
                        28 kilómetros de Cuzco, en la provincia de Urubamba, departamento de Cuzco, y antes de llegar al
                        Valle Sagrado de los Incas (y al río Urubamba) se encuentra el poblado de Chinchero. El mercado
                        dominical, que originalmente estuvo dominado por el trueque de productos entre los habitantes de
                        la zona, actualmente es un atractivo turístico por la oferta de sus artesanías incas y textiles
                        elaborados al estilo precolombino.<br><br>
                        Las importantes ruinas incas del pueblo fueron excavadas y restauradas por la Misión
                        Arqueológica Española entre 1968 y 1970. Esas investigaciones dieron lugar a varios volúmenes
                        publicados por el Ministerio de Asuntos Exteriores español.
                    </p>
                    <h2>Altitud de Chinchero en Perú</h2>
                    <p class="p-salkantay">
                        La altitud de Chinchero en Perú es de casi 4000 metros sobre el nivel del mar.
                    </p>
                    <h2>Clima en Chinchero en el Valle Sagrado</h2>
                    <p class="p-salkantay">
                        El clima es fresco, en días soleados la temperatura alcanza los 20°C. El clima del Pueblo
                        Chinchero en Perú es semiseco y templado con una temperatura promedio de 9.5°C. <br><br>
                        La temperatura media anual es de una máxima de 13°C y una mínima de 10°C. Durante el año se
                        observan dos estaciones: en la temporada de verano (octubre-marzo) las lluvias son abundantes y
                        constantes, con temperaturas que varían entre los 3° y 18 °C, durante el invierno (de abril a
                        septiembre) cesan las lluvias y el frío se acentúa por la noche llegando a niveles bajo cero,
                        pero durante el día el cielo se torna azul y despejado con temperaturas que alcanzan los 17 °C y
                        el clima es gélido.<br><br>
                        La mejor temporada para visitar el Pueblo de Chinchero en Perú es entre los meses de mayo a
                        septiembre, muy buena época porque el clima es templado y se pueden apreciar mejor los paisajes
                        que se van a recorrer.
                    </p>
                    <h2>Agricultura</h2>
                    <p class="p-salkantay">
                        Sus fértiles tierras los convierten en excelentes productores de papa, ollucos, ocas, habas,
                        cebada y trigo, productos tradicionales cuya siembra se remonta a la época de los Incas y que
                        aún hoy son el eje del comercio agrícola de la zona.
                    </p>
                    <h2>La Vestimenta</h2>
                    <p class="p-salkantay">
                    <h3>El hombre de Chinchero</h3>
                    Viste el típico chullo multicolor, especialmente los domingos y festivos también usa ponchos de lana
                    de oveja color nogal, chalecos y pantalones de tela. En los pies calzan las viejas sandalias
                    (chanclas), ya sean de cuero o de goma.

                    <h3>La Mujer de Chinchero</h3>
                    Por su parte, ha heredado el arte de hilar y tejer la lana por generaciones. Por ello, conserva con
                    orgullo su vestimenta tradicional. Viste llicllas (especie de mantas oscuras decoradas con
                    filigranas rojas y verdes y un alfiler a la altura del pecho), chalecos y faldas de tela negra,
                    sujetas al cuerpo con fajas o chumpis. Sobre sus cabezas, adornadas con finas trenzas, llevan
                    coloridas diademas.
                    </p>

                    <h2>¿Cómo llegar a Chinchero?</h2>
                    <p class="p-salkantay">
                        El primer paso, por supuesto, es ir en avión de Lima a Cusco, un viaje que dura 1 hora y 20
                        minutos. También se puede ir en bus a la ciudad imperial, pero el recorrido largo dura 1 día
                        completo. <br>
                        Una vez en Cusco se debe tomar un bus desde Avenida Grau o Calle Pavitos con dirección a
                        Urubamba, pasa por Chinchero luego de un viaje de 40 minutos. Pídele al conductor que te avise
                        cuando llegues a Chincheros. Para llegar al Centro Arqueológico hay que caminar 200 metros desde
                        la Plaza de Chincheros.
                    </p>
                    <h2>Cultura y tradición</h2>
                    <p class="p-salkantay">
                        Chinchero es el pueblo más típico y pintoresco del Valle Sagrado de los Incas, reúne a doce
                        comunidades indígenas andinas que aún se rigen por el sistema de organización inca llamado
                        "Ayllu". Su población se dedica al cultivo de especies autóctonas y a la ganadería, aunque hoy
                        en día el turismo mueve gran parte de la economía local. Un pueblo que lucha por mantener viva
                        su antigua tradición.<br><br>
                        Chinchero se caracteriza por la cultura viva que aún mantiene a pesar de la fuerte incursión del
                        mundo moderno en las tradiciones. Los domingos se realiza una feria en la plaza de armas de
                        Chinchero, donde verás productos del pueblo, artesanías y sobre todo sus textiles, que son
                        elaborados al estilo ancestral, y donde aún es posible observar el trueque. Si lo desea, puede
                        ver en los centros textiles cómo las mujeres ataviadas con sus hermosos trajes realizan el
                        proceso de hilado y teñido de la lana con tintes naturales, y apreciar la forma en que los
                        tejedores de Chinchero siguen la tradición milenaria.<br><br>
                        En la feria también podrás observar a los “Varayoc” que son los líderes de las comunidades que
                        forman parte de Chinchero, quienes ataviados con sus trajes típicos y multicolores siguen
                        celosamente manteniendo la tradición de sus antepasados.<br>
                    </p>

