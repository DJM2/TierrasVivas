                    <p class="p-salkantay">
                        Piscacucho en Cusco o el Km 82 en Perú, es el lugar donde comienza el Camino Inca a Machu
                        Picchu.<br><br>

                        El Camino Inca a Machu Picchu o Qhapaq Ñan es la ruta de trekking y camping con 39 km de
                        recorrido.<br><br>

                        El camino más largo comienza en Piscacucho (Km 82 en Perú) de la vía férrea a Machu Picchu.
                        Atraviesa diferentes ecosistemas, colosales sitios arqueológicos y lugares ricos en flora y
                        fauna, hasta llegar a la ciudadela de Machu Picchu.</p>

                    <h2>¿Donde es Piscacucho?</h2>
                    <p class="p-salkantay">
                        ASeguramente te estarás preguntando ¿dónde está Piscacucho? La comunidad de Piscacucho, también
                        conocida como Km 82 en Perú, está ubicada en el distrito de Ollantaytambo, provincia de Urubamba
                        en la Ciudad del Cusco, a una altura de 2709 msnm, en el borde del Parque Nacional Machu Picchu,
                        y donde los visitantes iniciar el Camino Inca, se puede llegar en bus en aproximadamente 3 horas
                        desde la ciudad del Cusco.
                    </p>

                    <h2>Km 82 del Camino Inca</h2>
                    <p class="p-salkantay">
                        Piscacucho en Cusco tiene un ecosistema que corresponde a zonas montañosas alpinas o montañosas,
                        su clima es templado, oscilando entre 15 y 20 grados centígrados en la mañana y 6 a 10 grados
                        centígrados en la noche.<br><br>

                        La temporada de lluvias cubre el período de noviembre a marzo, mientras que el resto del año es
                        relativamente seco.<br><br>

                        El Km 82 del Camino Inca tiene maravillosos paisajes andinos, los nevados y el río Urubamba que
                        adornan el paisaje de esta comunidad.<br><br>

                        En el Km 82 del Camino Inca se pueden observar sitios arqueológicos incas y caminos de
                        herradura, estando al inicio del camino Inca.<br><br>

                        En 2011 la “Dirección Regional de Cultura” realizó importantes hallazgos en esta zona, se
                        trataba de cráneos, huesos largos, y no se descartó que pudieran ser momificados ya que se
                        encontraban en buen estado.
                    </p>
