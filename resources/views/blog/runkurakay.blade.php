                    <h3>¿Como eran las ruinas de Runkurakay?</h3>
                    <p class="p-salkantay">
                        Estructuralmente, el complejo arqueológico Runkurakay presenta estructuras hechas con el uso de
                        piedra pizarra y granito gris. El edificio predominante del conjunto tiene forma de semicírculo
                        en el que se distribuyen seis estancias que rodean un patio orientado al noreste.<br><br>

                        A los lados de la entrada al patio se ubican dos de los seis recintos mencionados, donde es
                        posible observar la presencia de nichos y ventanas, un espacio común de las estructuras incas
                        donde, en su mayor parte, solían encontrarse ídolos adentro. Como parte del complejo, también
                        hay una sala inferior sobre la que se eleva una plataforma flanqueada por habitaciones más
                        pequeñas a cada lado. Además de la estructura es similar al complejo, donde se observan los seis
                        ambientes, también se reconoce como parte de las estructuras un ambiente de forma más
                        rectangular que se ubica fuera del grupo y casi en la parte baja del terreno ocupado por
                        Runkurakay.
                    </p>
                    <h3>¿Cómo son las ruinas de Runkurakay?</h3>
                    <p class="p-salkantay">
                        Las ruinas de Runkurakay no solo es un complejo arqueológico inca digno de conocer por su valor
                        arqueológico, sino también en cuanto a aspectos ecológicos, ya que su entorno permite disfrutar
                        de una exquisita y variada flora y fauna, por lo que las visitas al complejo también son
                        recomendadas para aquellos a quienes les gusta el ecoturismo, la fotografía ambiental, etc.

