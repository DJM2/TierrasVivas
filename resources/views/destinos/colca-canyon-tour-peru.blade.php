                        rústicas y viven en chozas. Taquile por otro lado se encuentra en el centro del lago, a cuatro
                        horas de la ciudad de Puno. La isla es muy especial; sus habitantes hablan quechua (el idioma de
                        la región) y ocasionalmente aymara. La gente son principalmente pescadores y tejedores que
                        visten hermosos atuendos multicolores como el chumpi o la lliclla. En la isla no hay ruido,
                        policías, contaminación, electricidad ni perros. Recomendamos tomar el desafío de quedarse en
                        Taquile y disfrutar de unos días tranquilos lejos del abrumador mundo moderno.
                    </p><br>

                    <P class="p-salkantay">
                        Cuatro horas al norte de Arequipa se encuentra el Cañón del Colca, un valle interandino de más
                        de 3400 metros de profundidad y rodeado de llanuras andinas y picos nevados. El Cañón del Colca
                        es el cañón más profundo del mundo. El Colca supera al Cañón del Colorado en los Estados Unidos
                        en más del doble de su profundidad.
                        La ruta al Cañón pasa por las reservas nacionales de Salinas y Aguada Blanca, hermosos
                        territorios donde habitan camélidos y otras especies andinas. </P><br><br>
