                    <h3>Información general del Camino Inca</h3>
                    <p class="p-salkantay">
                    <ul>
                        <li>Ubicación: Departamento de Cusco, Provincia de Urubamba 2,400 msnm (Machu Picchu)</li>
                        <li>Clima templádo</li>
                        <li>Temperatura media: 15 °C</li>
                        <li>Temporada: Recomendada entre los meses de Abril a Octubre, cerrado por mantenimiento durante
                            el mes de Febrero</li>
                        <li>Acceso: Vías aéreas y terrestres, desde Lima y Arequipa hasta Cusco. Ferrocarril, Poroy -
                            Piscacucho (Km 82) y Machu Picchu - Poroy (ida y vuelta, 92 km en 3 horas 30 minutos)</li>
                        <li>Duración: 4 días</li>
                    </ul>
                    </p>
                    <p class="p-salkantay">
                        Para iniciar el Camino Inca se inicia en la comunidad de Piscacucho (Km 82), que es la entrada
                        oficial al Camino Inca, desde aquí se inicia el legendario camino de alrededor de 6 a 7 horas,
                        11 kilómetros de largo, pasando por un paisaje impresionante y atravesando de el complejo
                        arqueológico inca de Qorihuayrachina con una maravillosa vista de Llactapata. Luego ascendemos a
                        la comunidad de Wayllabamba para finalmente descansar en el campamento de Yuncachimpa. El
                        segundo día la caminata es más al paso del caminante por el sendero Lulluchampa a 3,850 msnm,
                        hasta llegar a muchas escaleras, pasando por hermosos paisajes para llegar a Warmihuañusca a
                        4,200 msnm, punto a partir del cual se desciende pasando Runkurakay hasta llegar al Campamento
                        Chaquicocha. El tercer día de caminata es el más impresionante al adentrarse en los bosques
                        tropicales, combinado con hermosos monumentos arqueológicos incas como Sayacmarca, Conchamarca y
                        Phuyupatamarca. Finalmente, desciende unos escalones hasta el sitio arqueológico inca de
                        Wiñayhuayna hasta el último campamento. Para el cuarto día, caminamos un sendero de 7 kilómetros
                        de aproximadamente 2 a 3 horas, para finalmente llegar a la entrada a la magnífica ciudadela
                        inca de Machu Picchu llamada Inti Punku o Puerta del Sol.
                    </p>
