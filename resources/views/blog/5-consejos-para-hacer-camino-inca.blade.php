                    <h2>Reserve con 4 a 3 meses de anticipación</h2>
                    <p class="p-salkantay">
                        Así que en cuanto se tome la decisión, no olvide hacer la reserva de vuelos, alojamiento y el
                        recorrido antes. Esta ruta atrae a miles de personas de todo el mundo por lo que suele haber
                        mucha demanda la mayor parte del año. Los meses de Abril, Mayo, Junio, Julio, Agosto y Diciembre
                        son las fechas mas solicitadas.
                    </p>

                    <h2>Aclimatación</h2>
                    <p class="p-salkantay">
                        Durante varios días estarás a miles de metros sobre el nivel del mar. Significa que su cuerpo
                        necesita aclimatarse lentamente para evitar el molesto mal de altura. Es recomendable permanecer
                        en la ciudad de Cusco por lo menos 2 días antes de que la caminata comience. Debes saber que la
                        fecha del Camino Inca, no se puede cambiar y no hay devolución en caso de ausencia por lo que es
                        fundamental tomar precauciones para no perder la oportunidad de realizar la caminata.
                    </p>
