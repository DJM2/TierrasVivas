                    Wiñay Wayna es uno de los sitios arqueológicos más bellos. Lo que lo hace especial es que apenas
                    hay un alma allí, además de que las terrazas están construidas en una formación de anfiteatro
                    realmente genial que te permite ver toda el área. Estas terrazas fueron construidas para
                    cultivar diferentes cultivos y también encontrarás un templo dedicado al arco iris. También
                    puedes ver Intipata en la distancia (este es otro sitio arqueológico que se encuentra a
                    2.750 metros). Disfrutarás de la sección del sendero desde la cascada hasta Wiñay Wayna porque
                    el paisaje cambia drásticamente. A medida que se acerque al agua, las temperaturas se volvieron
                    más frías,
                    la vegetación se volvió más verde y, a veces, se llena como si estuviera caminando por un bosque
                    cubierto de musgo. Es un cambio bienvenido después de la temperatura cálida y el paisaje más
                    seco que has experimentado cerca del comienzo de la caminata por el Camino Inca.<br><br>

                    Después de visitar Wiñay Wayna, volverás al camino. Su próximo hito en el camino es Intipunku,
                    también conocido como la Puerta del Sol, y luego de un largo día de caminata, Aquí su grupo hace
                    una pausa por un momento para saborear el momento: ¡tendrá su primer vistazo
                    de Machu Picchu! y puedes tomar tus primeras selfies de Machu Picchu. Es un momento que ninguno
                    de ustedes olvidará.

                    Después de ver por primera vez Machu Picchu, continuará por el mismo camino, encontrará algunas
                    llamas y luego, una vez que llegue a la puerta de Machu Picchu, descenderá a Puente Ruinas,
                    donde su cocinero lo espera con una deliciosa cena y donde pasará una noche en la tienda, Machu
                    Picchu tendría que esperar hasta el día siguiente.
                    </p>
                    <h3>Día 02: Puente Ruinas – Machu Picchu – Cusco</h3>
                    <p class="p-salkantay">
                        Después de un desayuno temprano en la mañana, continuamos caminando por el Camino Inca durante
                        aproximadamente 2 horas hasta la misteriosa ciudad de Machu Picchu, en la que ingresamos para
                        las mejores oportunidades de fotos de la mañana. Luego, nuestro guía turístico nos muestra la
                        ciudad inca y nos explica su historia y sus propósitos. Después de la visita guiada, tenemos la
                        oportunidad de subir durante aproximadamente 45 minutos a Huayna Picchu o a la montaña Machu
                        Picchu (picos sobre Machu Picchu que sirven para tomar algunas fotos panorámicas de la ciudad
                        inca). Luego, tenemos tiempo libre para pasar y explorar Machu Picchu por nuestra cuenta.

                        El Camino Inca de 2 días termina con un viaje en tren a Ollantaytambo (alternativamente a
                        Poroy), desde donde nos llevan en camioneta de regreso a Cusco.
                    </p>
