                    <h2>Itinerario detallado para Choquequirao 4 días</h2>
                    <h3>DÍA 1 .- CUSCO – CAPULILLOC – CHIQUISCA – PLAYA ROSALINA</h3>
                    <p class="p-salkantay">
                        Salimos de Cusco aproximadamente a las 04.30 am, llegando al pueblo de Curahuasi donde tendremos
                        el primer desayuno, tiempo de llegada 3 horas aproximadamente. Luego nos dirigimos hasta el paso
                        de Capulilloc pasando por los pueblos de Saywite y Cachora; donde nos organizamos en nuestro
                        equipo en acémilas (mulas) donde empezaremos nuestra caminata descendiendo. Desde este sitio se
                        puede apreciar el majestuoso Padriyoc y Wayna Cachora picos nevados, también se puede visualizar
                        el hermoso cambio de paisajes desde precipicios hasta el Cañón del Apurimac con su impresionante
                        profundidad y en Chikiska tendremos el almuerzo con el staff de chef, después de degustar el
                        almuerzo nos dirigiremos a nuestro campamento de PLAYA-ROSALINA donde pasaremos la noche y
                        tendremos la cena.
                    </p>
                    <img src="{{ asset('img/galeria/caminata-a-choquequirao.webp') }}" loading="lazy"
                        alt="Caminata a Choquequirao" class="img-fluid"><br><br>
                    <h3>DÍA 2. – PLAYA ROSALINA – SANTA ROSA – MARAMPATA- CHOQUEQUIRAO</h3>
                    <p class="p-salkantay">
                        Temprano en la mañana después del desayuno, continuamos nuestra caminata ascendiendo por los
                        poblados de Santa Rosa y Marampata donde podremos apreciar un espectacular paisaje y desde
                        Marampata continuamos hacia el complejo arqueológico de Choquequirao aproximadamente 01.30
                        minutos, en el transcurso de la caminata se puede visualizar el complejo de Choquequirao cerca
                        de este complejo tendremos nuestro almuerzo y campamento, luego del almuerzo nos dirigiremos
                        hacer el recorrido guiado toda la tarde de la construcción inca “choquequirao” momento para
                        disfrutar de este majestuoso lugar, donde podemos apreciar casas, terrazas, plaza principal y el
                        sector de llamas y tal vez podemos observar «El vuelo de los cóndores».
                        El guía le explicarán la historia y la importancia del sitio y culminado el tours retornamos a
                        nuestro campamento ya mencionado.
                    </p>
                    <h3>DÍA 3.- CHOQUEQUIRAO – CHIKISKA</h3>
                    <p class="p-salkantay">
                        Después de un desayuno temprano, dejamos Choquequirao y descendemos de regreso donde
                        observaremos una vez más cañón del Apurímac, la exuberante vegetación, sus flores y orquídeas
                        arrivando a PLAYA el lugar de el almuerzo y luego tendremos una siesta prolongada
                        aproximadamente de 1.00 hora para continuar con nuestro caminata ascendiendo a nuestro
                        campamento de CHIKISKA donde tendremos la cena y pasaremos la noche.
                    </p>
                    <h3>DÍA 4. – CHIKISKA – CAPULIYOC – CUSCO</h3>
                    <p class="p-salkantay">
                        Temprano en la mañana después del desayuno seguimos nuestro camino ascendiendo en el cual
                        podemos apreciar nuevamente el majestuoso Padriyoc nevado que decora el Cañón Apurimac, donde
                        llegando a Capuliyoc donde tendremos el almuerzo y esperaremos el transporte el cual nos
                        retornara a Cusco.
                        * Los camping están sujetos a cambios de acuerdo con el tamaño del grupo y la condición física.
                    </p>
