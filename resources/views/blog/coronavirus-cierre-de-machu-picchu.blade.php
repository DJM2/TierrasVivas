                    <h2>Salud y seguridad</h2>
                    <p class="p-salkantay">
                        Machu Picchu y el Camino Inca son impresionantes 300-700 metros (980-2,300 pies) más bajos que
                        Cusco. Pero para estar seguro sobre los efectos de la altitud, conocidos localmente como
                        soroche, obtenga una amplia ingesta de líquidos y elimine o minimice el consumo de alcohol y
                        cafeína. Fumar agrava el problema. Algunos hoteles cuentan con un suministro de oxígeno para el
                        uso de sus huéspedes. El medicamento acetazolamida puede ayudar a compensar la alcalosis causada
                        por el bajo nivel de oxígeno en grandes alturas. El agua del grifo no es segura para beber.
                        Quédese con la variedad embotellada, ya sea ‘con gas’ (carbonatada) o ‘sin gas’ (todavía). La
                        marca de agua San Luis está disponible en casi todas partes.
                    </p>
