                    <h3>Chinchero:</h3>
                    <p class="p-salkantay">
                        Este poblado está ubicado a 28 km del Cusco sobre la ruta asfaltada a Urubamba. En donde se
                        encuentran los restos de lo que fuera la hacienda real de Túpac Inca Yupanqui, es como un bello
                        templo colonial levantado sobre basamentos incaicos. Su mayor atractivo sin embargo, es su
                        mercado artesanal, originariamente dedicado al trueque de productos agrícolas entre los
                        comuneros del valle y los de las partes altas. Actualmente, el mercado es un verdadero
                        espectáculo de color y movimiento que causa la fascinación del turista con su oferta de
                        artesanías y prendas textiles elaboradas del más puro estilo precolombino. Aquí también los
                        turistas pueden formar parte de la maravillosa demostración del teñido natural de la lana, los
                        cuales son teñidos de forma natural con plantas, semillas y minerales.
                    </p>
                    <h3>Moray:</h3>
                    <p class="p-salkantay">
                        Está ubicado a 7 km de Maras a 01 hora de la ciudad del Cusco por la ruta de Chinchero es una de
                        las más espectaculares visitas en donde existe andenes circulares de diferentes tamaños. Son
                        terrazas de experimentación agrícola y lugares de concentración de energía femenina, por la que
                        se considera como un centro magnético de la Pachamama.
                    </p>
                    <h3>Salineras de Maras:</h3>
                    <p class="p-salkantay">
                        Ubicado al noroeste del pueblo de Maras Conocida por algunos como “Minas de sal” están
                        constituidas por unos 3,000 pozos pequeños. La sal es obtenida desde la época prehispánica
                        mediante un sistema de secado al sol del agua salada que proviene de un riachuelo subterráneo.
                        Una vez evaporada el agua, la sal está lista para su venta.
                    </p>
                    <h3>Ollantaytambo:</h3>
                    <p class="p-salkantay">
                        Típico poblado inca ubicado a 21 km. de Urubamba (2,800 m s.n.m). Fue nombrado en honor al
                        cacique Ollanta, famoso por su romance con una princesa inca, hija de Pachacútec. Una de sus
                        zonas mejor conservadas se extiende al norte de la plaza Hanan Huacaypata, un total de 15
                        manzanas de casonas levantadas sobre muros de piedra labrada. En el vasto sitio arqueológico,
                        situado en la colina que denomina el pueblo, destacan edificios como el Templo del Sol, El
                        Intihuatana y los Baños de la Princesa. De Ollantaytambo parte un camino afirmado, que asciende
                        al Abra Málaga (4.200 m s.n.m) y se interna en dirección a la ceja de montaña, atravesando
                        poblados como el pintoresco Huilloc, hogar de renombrados Huairuros.
                    </p>
                    <h3>Pisaq:</h3>
                    <p class="p-salkantay">
                        Pisaq se ubica a 33 km del Cusco por carretera asfaltada (del Valle Sagrado de los Incas).
                        Cuenta con un pueblo antiguo, un sitio arqueológico considerado entre los más importantes del
                        Cusco, y un pueblo moderno, de origen colonial. Este lugar también cuenta con un mercado
                        artesanal de Pisac muy famoso que atrae a miles de visitantes y a comuneros procedentes de
                        remotos poblados, usando las coloridas vestimentas tradicionales. Aquí se recomienda comprar
                        todos los trabajos artesanales, ya que estos son mas económicos que en la ciudad del Cusco.
                    </p>
