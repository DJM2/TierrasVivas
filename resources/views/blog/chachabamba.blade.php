                    <h2>Diseño de la Ciudadela de Chachabamba en Perú</h2>
                    <p class="p-salkantay">
                        Los incas fueron constructores bastante hábiles. Pudieron vincular una ciudadela a la forma de
                        las montañas. Chachabamba en Perú no es una excepción. Es muy interesante e ingenioso, porque
                        está ubicado en lo alto de una colina. Esta parte
                        superior era plana o aplanada de alguna manera. Aunque la mayor parte del sitio se asienta sobre
                        una superficie plana, hay construcciones fascinantes que se adaptan a los contornos curvos de la
                        montaña. Como en todos sus sitios, todo está bien planificado y el diseño en sí es eficiente y
                        funcional.<br><br>

                        Algunos de los edificios, sin embargo, siguen las ondulaciones del terreno, son edificios
                        curvos. Sus características arquitectónicas se combinan, según las teorías actuales de los
                        arqueólogos, es decir, religiosas y militares. Sus construcciones de piedra son muy altas, bien
                        construidas, imponentes y en el pasado estaban
                        techadas con algún material ligero y flexible.<br><br>

                        Las ruinas de Chachabamba en el Camino Inca contienen dos tipos de arquitectura: las estructuras
                        imperiales incas y las domésticas. Por el tipo de arquitectura, estructuras y distribución,
                        probablemente fue un centro religioso donde los incas realizaban ceremonias dedicadas a la madre
                        naturaleza espacialmente en las montañas. En medio de la construcción hay una plaza muy linda
                        que en este sitio se puede ver un altar muy lindo tallado en roca natural. A pocos metros de
                        aquí hay varios baños de agua ceremoniales. Probablemente el lugar donde los peregrinos incas se
                        dan una ducha purificadora para limpiar el alma antes de llegar a Machu Picchu.<br><br>

                        El paisaje que lo rodea es maravilloso, cordilleras verdes atravesadas por caminos estrechos y
                        nubes blancas arriba, un lugar lleno de silencio y paz. El área de Chachabamba es un valle muy
                        fértil, un gran lugar para cultivar alimentos y plantas tropicales y subtropicales,
                        espacialmente batatas, mandioca y plantas medicinales.
                    </p>
                    <h2>Historia de Chachabamba en Perú</h2>
                    <p class="p-salkantay">
                        Las ruinas como Chachabamba en Perú fueron descubiertas en 1940, estando dentro de la ruta
                        tradicional del Camino Inca que seguía la corriente a lo largo de la costa del río Urubamba
                        hasta una altura subtropical de 2,171 metros sobre el nivel del mar en la región conocida como
                        Antisuyo.<br><br>

                        Por la forma de sus edificaciones y el orden arquitectónico que están contemplando, se puede
                        decir que Chachabamba también se habría construido con importantes propósitos religiosos.
                    </p>
                    <h2>Propósito de las ruinas de Chachabamba en Perú</h2>
                    <p class="p-salkantay">
                        “Se cree que un sitio arqueológico fue un lugar destinado al Tratado de Aguas” o que “sirvió
                        como lugar de refugio antes de ingresar a Machu Picchu”. Investigaciones arqueológicas e
                        históricas actuales sostienen que este sitio probablemente fue
                        construido por una de las siguientes dos razones: primero, como un fuerte militar para controlar
                        toda el área, porque era el acceso a las cercanías de Machu Picchu, que era el retiro de lujo
                        para el pueblo. emperador, su familia y el resto de la corte. En segundo lugar, como complejo
                        religioso para adorar el agua, era una deidad central para ellos, debido a la importancia
                        esencial de la agricultura para su supervivencia.<br><br>

                        Este punto culminante, el sitio de Chachabamba, según estudios arqueológicos, se construyó
                        primero como un sitio religioso importante para el culto al agua; los incas tenían una conexión
                        muy fuerte con la naturaleza, a la que llamaron Pachamama (Madre Tierra). Su economía se basaba
                        en la agricultura, por lo que tanto el culto a la Pachamama como la deidad del agua fueron muy
                        importantes en su visión del mundo.

                        En segundo lugar, el sitio de Chachabamba también era un puesto de control para las personas que
                        se dirigían al santuario de Machu Picchu; Machu Picchu era un lugar donde todos los líderes
                        incas se iban de “vacaciones”, por así decirlo, era un retiro real, por lo que también tomaron
                        algunas medidas de seguridad. El Inti Punku (Puerta del Sol), que es la entrada a Machu Picchu,
                        es también un puesto de control custodiado por soldados de élite.
                    </p>
