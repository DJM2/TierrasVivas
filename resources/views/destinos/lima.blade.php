                    <h3>Clima en Lima</h3>
                    <p class="p-salkantay">
                        El clima en Lima Perú es especialmente particular dada su situación. Combina una práctica
                        ausencia de precipitaciones, con un nivel muy alto de humedad atmosférica y una nubosidad
                        persistente. Así, sorprende por sus extrañas características a pesar de estar ubicada en una
                        zona Tropical a 12 grados de latitud sur y casi al nivel del mar. La costa central peruana
                        presenta una serie de microclimas atípicos debido a la influyente y fría Corriente de Humboldt
                        que deriva de la Antártida, la cercanía de la cordillera y la ubicación tropical, dotando a Lima
                        de un clima subtropical, desértico y húmedo al mismo tiempo.<br><br>

                        Se puede decir que el clima en Lima Perú es cálido sin excesivo calor tropical ni frío extremo
                        que requiere calefacción en casa, salvo muy pocos inviernos. La temperatura media anual es de
                        18,5 a 19 ° C, con una máxima anual de unos 29 ° C.Los veranos, de diciembre a abril, tienen
                        temperaturas que oscilan entre 28 y 21 ° C.Sólo cuando ocurre un Fenómeno del Niño, la
                        temperatura en verano puede superar los 31 ° C. Los inviernos van de junio a septiembre, con
                        temperaturas que oscilan entre 19° y 12 ° C, siendo 5 ° C la temperatura más baja históricamente
                        probada. Los meses de primavera y otoño (septiembre, octubre y mayo) tienen temperaturas suaves
                        que oscilan entre los 23 y 17 ° C.<br><br>

                        Por otro lado, la humedad relativa es extremadamente alta (hasta el 100%), produciendo niebla
                        persistente de junio a diciembre hasta la entrada del verano cuando las nubes son más bajas. Es
                        soleado, húmedo y caluroso en los veranos (diciembre-abril), nublado y templado en los inviernos
                        (junio a septiembre). La lluvia es casi nula. El promedio anual reportado en el aeropuerto es de
                        7 mm, siendo la cantidad más pequeña en un área metropolitana del mundo. Lima tiene solo 1284
                        horas de sol al año, 28.6 horas en julio y 179.1 horas en enero, valores excepcionalmente bajos
                        para la latitud. La combinación de fenómenos climáticos se presenta de la siguiente manera:
