                    <h2>Ruinas de Patallacta</h2>
                    <p class="p-salkantay">
                        Este conjunto de edificaciones arqueológicas está compuesto por 112 habitaciones, construidas
                        con piedras rústicas unidas con argamasa de arcilla, a las que se han incorporado piedras
                        talladas. Fue un sitio ceremonial y del sector agrícola. Este sitio albergaba a un gran número
                        de ocupantes, incluidos viajeros y soldados que manejaban
                        el “fuerte” cercano de Willkaraqay, y un santuario de paredes redondeadas conocido como
                        Pulpituyoc que tenía funciones religiosas y ceremoniales.<br><br>

                        Algunos especialistas creen que este lugar fue un complejo agrícola. Las Ruinas de Patallacta
                        tiene cuatro sectores muy diferenciados por la forma y organización de los edificios. Todos
                        estos sectores y subdivisiones estaban interconectados mediante carreteras, caminos y
                        escaleras. Patallaqta se ubica sobre una superficie plana más o menos elevada en la margen
                        derecha del río Urubamba.
                    </p>
