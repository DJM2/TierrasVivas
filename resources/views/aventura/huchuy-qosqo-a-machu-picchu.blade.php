                    <h3>Día 01: Cusco – Huchuy Qosqo – Aguas Calientes</h3>
                    <p class="p-salkantay">
                        Nuestro recorrido iniciara con el recojo del hotel entre las 6:30 a 6:45 am. A partir de las
                        7:00 am se dará inicio del tour. Teniendo como primer punto de parada el poblado de Chinchero,
                        podemos apreciar los extensos campos de cultivos, que se han cultivado desde la antigüedad.
                        Viajamos a lo largo de las orillas de la laguna Piuray, hacia la comunidad de Tauca (3762 msnm.)
                        donde comienza nuestra caminata.

                        Seguimos un sendero de tierra, con una suave pendiente hacia el paso Pucajasa (4393 msnm), donde
                        tendremos una vista espectacular de la cordillera de Urubamba. Este paso es de unos 200 metros
                        de largo. Comenzamos nuestro descenso y encontramos secciones del camino Inca y también podemos
                        apreciar cambios radicales en los niveles ecológicos.

                        Llegamos a Huchuyqosqo (3688 msnm), donde disfrutamos de nuestro box lunch. Después de un breve
                        descenso, comenzaremos nuestro recorrido por el complejo arqueológico, después descenderemos al
                        pueblo de Lamay (2938 msnm) donde nuestro conductor nos estará esperando para llevarnos a la
                        estación de trenes en Ollantaytambo. Abordaremos el tren y después de 1 hora y 40 minutos
                        llegamos a Aguas Calientes. Nuestro día termina aquí después de la cena y pernocte en el hotel.
                    </p>
                    <h3>Día 02: Machu Picchu – Cusco</h3>
