                    <p class="p-salkantay">
                        Durante la celebración del Inti Raymi en este tiempo, todo el centro de Cusco está cerrado al
                        tráfico regular, incluidos autobuses y taxis locales.

                        Solo se permiten vehículos turísticos para transportar turistas desde Cusco a Saqsayhuman.

                        Si asiste a Saqsayhuman de forma independiente, puede esperar caminar a Saqsayhuman desde Cusco,
