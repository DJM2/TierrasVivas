                        viaje por aproximadamente 40 minutos mientras observamos los paisajes, las misteriosas montañas
                        y los profundos valles.<br><br>

                        Llegamos al pueblo de Llaqto a 3,700 m s. n. m.. A partir de aquí se inicia la caminata cuesta
