                    <h3>Las lineas de Nazca que puedes ver</h3>
                    <ul>
                        <li>Ave grande: (305 m de largo y 54 de ancho) por lo que es el más grande de todos.</li>
                        <li>Lagarto: (188m) Está cortado a la mitad por la carretera Panamericana Sur. Le cortaron las
                            patas durante la construcción de esta carretera.</li>
                        <li>Pelícano: (138 m)</li>
                        <li>Mono: (135m) es peculiar porque solo tiene 9 dedos en ambas manos.</li>
                        <li>Cóndor: (135m)</li>
                        <li>Colibrí: La distancia entre sus alas es de 66 m.</li>
                        <li>Manos: (5m) similar al mono, puedes encontrar 9 dedos en ambos.</li>
                        <li>Flor: (76m)</li>
                        <li>Araña: (46m)</li>
                        <li>Figura humana: (40 m) también conocido como el astronauta.</li>
                        <li>Estas formas se pueden encontrar entre otras.</li>
