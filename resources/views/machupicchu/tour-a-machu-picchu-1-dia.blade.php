                    <h2>Itinerario completo Machu Picchu full day</h2>
                    <h3>Machu Picchu de 1 día:</h3>
                    <p class="p-salkantay">
                        A las 5:00 a. m. pasamos a recogerlos de su hotel en Cusco, los llevaremos a la estación de
                        trenes ubicado en la localidad de Ollantaytambo (1 hora 30 minutos de viaje en carro) lugar
                        donde abordamos nuestro tren para luego realizar nuestro viaje con destino al pueblo de
                        Machupicchu (2,000 m s.n.m.) que tiene una duración de 1 hora y 45 minutos.<br><br>

                        Al llegar a la estación de tren, nuestro guía los estará esperando con un cartel y sus nombres,
                        con él se dirige a la estación de buses, para ascender 400 m de altura, por un camino
                        zigzagueante, durante 25 minutos hasta llegar a la increíble Maravilla del Mundo «El Santuario
                        Histórico de Machu Picchu», conocida como «La Ciudad Perdida de los Incas».<br><br>
                        <img src="{{ asset('img/galeria/machu-picchu-tours.webp') }}" alt="Machu Picchu citadel"
                            loading="lazy" width="100%"><br><br>

                        Al llegar a la Ciudadela de Machu Picchu pasamos por el control donde usted entrega su boleto de
                        ingreso, desde este lugar se iniciará la excursión guiada, visitan los principales lugares como:
                        la Plaza Principal, la Torre Circular, el Sagrado Reloj Solar, los Recintos Reales, el Templo de
                        las Tres Ventanas y los cementerios, la visita guiada será por más de 2 horas.<br><br>

                        Al concluir se le dará tiempo libre para dar un paseo por la ciudadela y visitar Machu Picchu
                        por cuenta propia, tiene como opción hacer la caminata a la Puerta del Sol o Puente Inca, luego
                        retornamos al pueblo de Machupicchu para almorzar en los restaurantes (el almuerzo no incluye en
                        el paquete).<br><br>

                        Por la tarde toma el tren de regreso al Cusco (debe presentarse en la estación 30 minutos antes
                        de la hora de salida de su tren), el retorno seguirá el mismo trayecto que el viaje de ida, al
                        llegar a la estación de trenes en Ollantaytambo, lo estaremos esperando para llevarlo a su hotel
                        en Cusco (viaje de 1 hora y 20 minutos en carro). Estamos llegando al Cusco 8:00 p. m.
                        aproximadamente.<br><br>

                        Fin de los servicios.
                    </p>

