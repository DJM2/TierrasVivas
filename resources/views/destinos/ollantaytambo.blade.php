                    <h3>Sendero de Ollantaytambo</h3>
                    <p class="p-salkantay">
                        Este sendero autoguiado le permite recorrer el trazado original de la ciudad, siguiendo una
                        serie de placas azules que delinean sitios importantes. Atribuya la apariencia distintiva de la
                        ciudad a la organización Inca. Basaron sus comunidades en la unidad de la “cancha”, un bloque de
                        la ciudad amurallado, cada uno con una entrada que conduce a un patio interior, rodeado por un
                        conjunto de casas. El sistema es más evidente en el centro de la ciudad alrededor de la plaza
                        principal. Encontrarás la más acogedora de estas comunidades autónomas en la Calle del Medio.
                    </p>
