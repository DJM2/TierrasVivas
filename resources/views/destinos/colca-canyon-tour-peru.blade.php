                    <P class="p-salkantay">
                        El Cañón del Colca se ha convertido en uno de los lugares turísticos más importantes de
                        Arequipa. Un viaje de dos días lo llevará a Chival, importante ciudad del valle donde el
                        visitante podrá descansar y disfrutar de las aguas termales de La Calera o probar una deliciosa
                        trucha (del río). Los amantes de los deportes de aventura encuentran en los rápidos turbulentos
                        del río que cruza
                        el valle un desafío irresistible para el canotaje o kayak. En el valle, el clima puede variar de
                        frío a semitropical en las partes más profundas. Esta zona ha hecho posible el desarrollo de más
                        de 20 especies de cactus y 170 especies de aves. El ave más importante es el cóndor, considerada
                        la criatura voladora más grande del planeta. Su vuelo se puede observar en el famoso mirador de
                        “La Cruz del Cóndor”, a pocos minutos de Chivay. Aquí también se pueden ver las puntas del
                        Ampato Coropuna y el Mismi, cuyo hielo derretido da origen al río Amazonas. En los pueblos del
                        valle hay iglesias y fiestas tradicionales. El Colca se caracteriza por su especial artesanía:
                        bordados coloridos, piezas artesanales hechas de hojalata, vela y madera tallada.
                    </P>
