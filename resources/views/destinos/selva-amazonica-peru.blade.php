                    <h3>Parque Nacional de Manu</h3>
                    <p class="p-salkantay">
                        El pueblo de Atalaya se ubica a 280 km / 174 millas de la ciudad de Cusco tomando la Carretera
                        Cusco – Paucartambo (12 horas en vehículo 4×4). El viaje continúa por el río hasta la localidad
                        de Boca Manu (7 horas). Luego, debes navegar nuevamente por el río Madre de Dios para llegar al
                        parque. También es posible llegar al pueblo de Boca Manu tomando una avioneta desde Cusco (45
                        minutos).<br><br>

                        El Parque Nacional del Manu se extiende entre dos departamentos, Cusco y Valle Sagrado y Madre
                        de Dios, y cubre un área de 1,692.137 hectáreas (o toda la cuenca del río Manu).

                        Desde 1977, el Parque Nacional del Manu forma el Núcleo de la Reserva de la Biosfera del Manu,
                        la cual fue declarada Patrimonio Natural de la Humanidad por la UNESCO en 1987. Aunque no se
                        permite la entrada a nadie, los visitantes pueden alojarse en albergues autorizados en el Área
                        Reservada y en la Zona Cultural adyacente al parque.<br><br>

                        El parque cuenta con una gran variedad de especies animales: más de 800 especies de aves como el
                        águila harpía, la cigüeña jabiru, el ganso del Orinoco, el gallito de las rocas y la espátula
                        rosada, 200 especies de mamíferos como el lanudo común. mono, el mono araña negro, la nutria
                        gigante, el jaguar, el gatito moteado, el oso hormiguero y el venado andino, y más de cien
                        especies de murciélagos.

                        Además, hay árboles de más de 45 metros de altura y 3 metros de diámetro. Las especies más
                        características son el cetico, la topa, el cedro, el tornillo, el kapok blanco y el mata palo.

                        Hoy en día, 30 comunidades agrícolas de habla quechua se encuentran repartidas por todo el
                        Parque Nacional del Manu, así como numerosos pueblos originarios de la Amazonía como los
                        Matsiguenka, Amahuaca, Yaminahua, Piro, Amarakaeri, Huashipaire y Nahua.
                    </p>
                    <h3>Puerto Maldonado</h3>
                    <p class="p-salkantay">
                        <strong>Collpa de Guacamayos colorados</strong><br>
                        Dentro de la Reserva Nacional Tambopata ya 150 km / 93 millas de Puerto Maldonado.

                        La colpa (colpa de guacamayos) es un lugar donde se congregan los guacamayos, formados a lo
                        largo de las orillas del río debido a ciertos procesos de erosión que permitieron la formación
                        de suelos ricos en sales minerales. Tiene aproximadamente 50 metros (164 pies) de altura y 500
                        metros (1640 pies) de largo, y es considerado el más grande de la selva amazónica peruana. Todas
                        las mañanas se reúnen allí seis especies diferentes de guacamayos, loros y periquitos.

                        Estos coloridos pájaros vuelan alrededor de la collpa antes de iniciar el “colpeo”, que consiste
                        en comer la arcilla que se encuentra en el acantilado y que sirve como complemento nutricional.
                        Después de permanecer allí de veinticinco a treinta minutos, los pájaros vuelan para regresar al
                        día siguiente. De vez en cuando, van allí tapires, capibaras y ardillas brasileños. En la copa
                        de los árboles se pueden ver diferentes especies de monos como el aullador rojo, el caupuchin,
                        el titi y, en ocasiones, incluso monos araña.
                    </p>
                    <h3>Lago Sandoval</h3>
                    <p class="p-salkantay">
                        10 km / 6 millas de Puerto Maldonado Para llegar al lago se debe caminar 5 km 7 3 millas desde
                        la orilla del río hasta el interior (1
                        hora y 30 minutos). Tiene aproximadamente 3 km (2 millas) de largo, 1 km (0,6 millas) de ancho y
                        0,5 centímetros
                        (0,1 pulgadas) a 3 metros (10 pies) de profundidad. El agua se mantiene a una temperatura
                        promedio de 26ºC (79,8ºF), y allí vive una gran cantidad de peces. Está rodeado de aguajales,
                        áreas pantanosas donde, entre otras especies exóticas, crece una palmera nativa llamada
                        “aguaje”.<br><br>

                        Junto con la vegetación local, se pueden ver orquídeas, plátanos silvestres y palmas milpesos,
                        ceibos gigantes, caobas y palmeras de aguaje de 30 metros (98 pies).

                        En este hábitat habita una gran variedad de aves, como cormoranes, tucanes, guacamayos, loros,
                        chillones cornudos y garzas. Además, existe una colorida variedad de gallinas salvajes llamadas
                        hoatzin o shansho cuyas cabezas están rematadas con plumas. Con un poco de suerte, se pueden ver
                        tapires, tortugas y nutrias gigantes o “lobos de río”, así como diferentes especies de
                        cocodrilos, como el caimán negro.
                    </p>
                    <h3>Lago de Valencia</h3>
                    <p class="p-salkantay">
                        A 60 km de Puerto Maldonado El lago tiene 15 km (9 millas) de largo, 800 metros (2,625 pies) de
                        ancho y entre 0,5 y 15
                        metros (1,6 y 49 pies) de profundidad, y gracias a sus árboles y peces, es considerado un lugar
                        privilegiado. Alrededor del lago hay árboles como el pumaquiro, la quinilla, el cedro, la ceiba
                        gigante, la palmera de abanico enana y el castaño de Brasil. Entre su vida animal, las más
                        exóticas son las pavas barbudas, las tortugas (charapas y motelos cabezones), cocodrilos, monos,
                        cormoranes y garzas.<br><br>

                        La riqueza de sus aguas permite que tanto los nativos de Huarayos y como los pobladores de sus
                        alrededores se ganen la vida con la pesca del bagre , nariz de pala, palometas, bagre dorado,
                        pirañas y paiches. Estos últimos, no son una especie nativa sino que fueron traídos al lago.
                        Junto a la pesca, otra actividad económica importante es la recolección de castañas.
                    </p>
                    <h3>Parque Nacional Bahuaja-Sonene</h3>
                    <p class="p-salkantay">
                        A 90 km de Puerto Maldonado Este parque nacional esta con el fin de proteger la única sabana
                        húmeda tropical que existe en
                        el Perú. El parque está ubicado entre los departamentos de Madre de Dios y Puno en las
                        provincias de Tambopata, Carabaya y Sandia. Tiene una superficie de 1.091.416 hectáreas, ademas
                        una parte se encuentra en Bolivia. Los animales que mas destacas son el lobo de crin, el venado
                        de los pantanos , el oso hormiguero gigante, el lobo de rio y etc
                    </p>
