                    <h2>Itinerario Detallado Inca Jungle:</h2>
                    <h3>Día 1: Cusco – Abra Málaga – San Luis – Santa María.</h3>
                    <p class="p-salkantay">
                        Les recogemos a las 7:30 a.m. de vuestro hospedaje para luego partir en un bus hasta el ABRA
                        MALAGA (cuatro horas-4350 m.s.n.m.) un punto alto de la ruta donde podremos observar una vista
                        excelente del nevado VERONICA, luego seguiremos la ruta hasta el pueblo de SAN LUIS (2900
                        m.s.n.m.), donde tendremos un tiempo para almorzar, luego descenderemos en bicicletas por un
                        lapso de 04 a 05 horas, en la ruta podremos observar plantaciones de plátanos, naranjales, coca,
                        y otros frutales, también pasaremos por rústicos pueblos andinos; a las 5 o 6 de la tarde
                        llegaremos al Pueblo de SANTA MARIA, donde cenaremos y pernoctaremos la primera noche en una
                        casa familiar.
                    </p>
                    <h3>Día 2: Santa María – Santa Teresa.</h3>
                    <p class="p-salkantay">
                        Luego del desayuno continuaremos la ruta, este día realizaremos una caminata de 7 a 8 horas, en
                        primer lugar ascenderemos hasta el medio de la montaña donde apreciaremos los hermosos valles
                        selvático andinos y quebradas donde encontraremos plantas típicas, y algunas personas oriundas
                        de la zona que viven de los sembríos de café, también encontraremos un tramo de un ancestral
                        camino inca que recorreremos un tiempo aproximado de una hora, todo este tramo es empedrado,
                        donde también encontraremos las famosas escalinatas incas; luego llegaremos hasta QUELLOMAYO
                        donde tendremos un tiempo de descanso para el almuerzo, para luego seguir nuestra caminata,
                        cruzaremos ríos, algunos de estos en puentes colgante (oroyas), luego encontraremos las
                        placenteras aguas termales, donde tendremos tiempo libre para tomar un baño y relajarnos; luego
                        serán otros 45 minutos más de caminata para llegar a SANTA TERESA, en este lugar pernoctaremos
                        la segunda noche, (casa familiar).
                    </p>
                    <h3>Día 3: Santa Teresa-Aguas Calientes</h3>
                    <p class="p-salkantay">
                        Este día continuaremos la caminata para llegar al pueblo de Machupicchu, la caminata será de 5 a
                        6 horas, pasaremos por algunas cascadas, donde tendremos la opción de tomar un baño refrescante
                        luego llegaremos a HIDROELECTRICA, donde tendremos el almuerzo, para luego seguir nuestro rumbo
                        hasta llegar al pueblo de Machupicchu (AGUAS CALIENTES), aquí tendrán la tarde libre para
                        descansar o también tienen la opción de subir a la montaña PUTUKUSI, que está localizada al
                        frente de Machupicchu; por la noche recibiremos la cena y descansaremos pensando ya en el tour
                        del día siguiente “Machupicchu”, pernoctamos en un hostal.
                    </p>
                    <h3>Día 4: Machupicchu-Cusco.</h3>
                    <p class="p-salkantay">
                        Nos levantamos muy temprano (4:30 a.m.) para comenzar a subir hasta el centro arqueológico, la
                        caminata dura una hora y diez minutos, llegaremos temprano para ver la salida del sol, luego de
                        tomar las primeras fotos comenzaremos el tour guiado que durara entre dos a dos horas y media,
                        después tendrán tiempo libre (4 a 5 horas) para poder subir a la montaña WAYNAPICCHU, o
                        simplemente descansar y tomar más fotos, por la tarde descendemos nuevamente hacia el pueblo de
                        Aguas Calientes donde tendrán tiempo libre para almorzar, luego tomaremos nuestro tren de
                        retorno (02 horas)que nos traerá hasta Ollantaytambo, allí nos estarán esperando para traernos
                        en un bus (01 hora y media) hasta el Cusco (9:30 pm.).
                    </p>
