                    <p class="p-salkantay">
                        Antes del amanecer en el lago Titicaca, una neblina azul cubre nuestra vista. Muchos conejillos
                        de indias corren para esconderse debajo de las rocas. A esta maravillosa vista le siguen patos y
                        chocas que hacen sonidos con sus alas como si estuvieran a punto de volar. De repente, la luz
                        del sol golpea el suelo y te permite saber que es un nuevo día, es el paraíso en la Tierra. El
                        lago Titicaca está ubicado en el departamento de Puno a 3.808 m sobre el nivel del mar, es el
                        lago navegable más alto del mundo y sin duda uno de los mayores símbolos de los habitantes de
                        Puno. Su parte más profunda está junto a la isla de Soto y alcanza los 280 metros de
                        profundidad. La importancia del lago Titicaca radica en sus características geográficas e
                        históricas que también regulan la temperatura en las zonas cercanas y también funcionan como
                        recursos útiles para sus habitantes.
                        <br><br>
                        <img src="{{ asset('img/galeria/lago-titicaca.webp') }}" alt="Lago Titicaca Puno"
                            loading="lazy" width="100%">
                        <br><br>

                        Este lago es el cuerpo de agua más sagrado del Imperio Inca y ahora la separación natural entre
                        Perú y Bolivia, centro de una región donde miles de agricultores de subsistencia se ganan la
                        vida pescando en sus heladas aguas. Por la noche hace mucho frío, con temperaturas bajo cero, de
                        junio a agosto. Durante el día, el sol es intenso y las quemaduras solares son comunes por lo
                        que es importante protegerse. El lago contiene una gran cantidad de especies animales como patos
                        silvestres, diferentes aves y peces, el carachis, la totora, planta comestible utilizada para
                        construir islas flotantes, casas y caballitos de totora (embarcaciones típicas) son los más
                        representativos de todos los Las islas del lado peruano del lago, Taquile, Uros, Suasi y
                        Amantani son las más importantes.<br>

                        Una de las “culturas” más importantes que vivió en la ribera del río Titicaca fue la Cultura
                        Chiripa de la cual se pueden encontrar importantes restos arqueológicos en la parte sur del lago
                        entre otros importantes que vivieron allí durante siglos. Hoy en día, este lago debe enfrentar
                        las consecuencias del aumento de población y el desarrollo emergente de la ciudad de Puno que
                        aún vierte aguas residuales en el lago sin tratamiento previo. Como parte de un nuevo movimiento
                        de concientización sobre la contaminación se creó la Reserva Nacional Titicaca en el mes de
                        octubre de 1978 la cual se divide en dos áreas Ramis ubicadas en la provincia de Huancane en el
                        lado norte del lago y en el área de la bahía.<br>

                        Perú tiene diferentes especies de peces y animales silvestres en julio de 2015 se realizó un
                        estudio en el lago y mostró que la especie nativa de peces Ispi que se encuentra en peligro
                        logró duplicar su población. Los especialistas consideran que estas son buenas noticias y que el
                        lago es mágico. En el lago se pueden encontrar diferentes tipos de especies como, parihuanas,
                        zambullidor de Titicaca y muchas más.
                        <br><br>
                        <img src="{{ asset('img/galeria/tour-en-lago-titicaca.webp') }}" alt="Lago Titicaca Puno"
                            loading="lazy" width="100%">
                        <br><br>

                        Cuando visite Perú y el lago Titicaca, tendrá un encuentro increíble con la naturaleza y los
                        diferentes lugares que verá allí. Por ejemplo, la isla Amantani, ubicada a 36 kilómetres del
                        puerto de la ciudad de Puno frente a Capachica. La isla está llena de diferentes arbustos. Es
                        interesante visitar los dos miradores naturales donde se pueden observar restos prehispánicos,
                        el templo de Pach Tata, Pacha Mama, Incachincana, Inca tihana y el cementerio de las momias.<br>

                        También se encuentra la Isla Taquile, ubicada a 35 kilómetros de Puno en la bahía de Chunchiyo.
                        Este lugar fue utilizado como prisión política hasta los primeros años del siglo XX. Isla
                        Esteves, se encuentra a 65 kilómetros de Puno. Posee importantes restos arqueológicos de las
                        culturas Tiahuanaco, Colla y Pucara. Muchos años después, fue utilizado como prisión por los
                        españoles.<br>


                        Restos arqueológicos de Inca Uyo, ubicados a 18 kilómetros de Puno. También conocido como el
                        templo de la fertilidad. Una de sus impresionantes características es su construcción con
                        piedras talladas de casi 2 metros de altura. Según los investigadores se utilizó como un centro
                        ceremonial muy importante.<br>
