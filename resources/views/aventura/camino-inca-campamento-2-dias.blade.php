                    <h3>Día 01: Cusco – Ollantaytambo – Km 104 – Puente Ruinas</h3>
                    <p class="p-salkantay">
                        El primer día de la caminata comienza mucho antes de que salga el sol. Temprano en la mañana
                        entre las 05:00 a 05:30. Nuestro guía y conductor lo recogerá de su hotel y lo llevará hasta
                        Ollantaytambo, donde tomaría el tren en dirección a Aguas Calientes. Se trata de una unidad de 2
                        horas. Cuando llegue a Ollantaytambo entre las 06:30 y las 7:00 a.m., puede tomar un desayuno o
                        tomar un café. y abordó el tren. donde encuentras los cómodos asientos. No solo eso, sino que
                        también hay ventanas adicionales en el techo para que pueda disfrutar de las montañas que se
                        avecinan en el camino.<br><br>

                        Estará en el tren durante aproximadamente una hora y media antes de llegar a nuestra parada:
                        Kilómetro 104. Nuestro guía tiene los permisos especiales del Camino Inca para que los
                        excursionistas puedan ingresar al Camino Inca. y caminó hacia las orillas del río Urubamba,
                        donde te saludan con un puente de madera que conduce a la espesa jungla y algunas nubes colgando
                        en el aire de la mañana. Tan pronto como nos registremos en el sendero, se encontrará con su
                        primer sitio arqueológico del día: Chachabamba. Este sitio se encuentra a 2,170 metros y fue uno
                        de los últimos refugios que usaron los incas antes de llegar a Machu Picchu. También funcionaba
                        como centro religioso y administrativo.<br><br>

                        Después de visitar Chachabamba, comenzará el lento ascenso en los escalones de piedra inca. El
                        sendero es empinado durante las primeras 2 horas, pero eventualmente se nivela (por algunos
                        breves tramos, de todos modos). Durante ese tiempo, se detendrá en 2 refugios diferentes donde
                        se refrescará a la sombra y disfrutará de algunos refrigerios. A la hora del almuerzo llegamos a
                        una cascada y desempacamos nuestros almuerzos que le proporcionarán su guía. Desde allí,
                        continuó hasta Wiñay Wayna, que se encuentra a 2.650 metros. Este es el punto culminante del
                        Camino Inca!<br><br>
