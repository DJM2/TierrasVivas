                        de ruinas y dos o tres cabañas modernas. Los indios decían que el lugar se llamaba Llacta Pata”.
                        Sin embargo, Bingham no investigó las ruinas a fondo y no se volvieron a estudiar durante otros
                        70 años.</p>
