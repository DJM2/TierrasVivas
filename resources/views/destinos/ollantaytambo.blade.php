                    <h3>Historia de Ollantaytambo</h3>
                    <p class="p-salkantay">
                        El pueblo se pronuncia “oy-yahn-tie-tahm-bo”, pero todos lo llaman “Ollanta” para abreviar. Fue
                        nombrado por Ollantay, el general inca que expandió las fronteras del Tawantinsuyo hasta
                        Colombia y al sur de Argentina durante el reinado del Inca Pachacutec. El general pidió la mano
                        de la hija del emperador, petición que Pachacutec rechazó. Lo logró a través de Ollantay y
                        todavía era un plebeyo. El general se rebeló contra el gobernante y fue encarcelado. El amor de
                        Ollantay puede haber tenido un mal final. Pero el tuyo no lo hará cuando vislumbres las calles y
                        casas de piedra, el paisaje montañoso, algunos de los territorios más exuberantes del Valle, y
                        las grandes ruinas.<br>

                        Al pueblo de Ollantaytambo se le llama “Ciudad Inca Viva”, porque sus habitantes mantienen vivos
                        hábitos y costumbres muy ancestrales.
                    </p>
                    <h3>Fortaleza de Ollantaytambo</h3>
                    <p class="p-salkantay">
                        Camine sobre la ciudad hasta una formidable estructura de piedra donde enormes terrazas suben
                        hasta la cima. Era la principal defensa del valle contra las tribus Antis de las selvas
                        tropicales vecinas. La construcción comenzó durante el reinado de Pachacutec pero nunca se
                        completó. El granito de color rosa utilizado no se extrajo en la parte del Valle. El elaborado
                        complejo amurallado contenía un templo al sol, utilizado para la observación astronómica, así
                        como los Baños de la Ñusta (baños ceremoniales de princesas), lo que llevó a los arqueólogos a
                        creer que Ollantaytambo existía con fines más que defensivos.<br><br>

                        La fortaleza fue el sitio de la mayor victoria Inca sobre los españoles durante las guerras de
                        conquista. El Manco Inca huyó aquí en 1537 con un contingente de tropas después de la desastrosa
                        pérdida en Sacsayhuaman y derrotó a las fuerzas españolas al mando de Hernando Pizarro. La
                        victoria duró poco: Pizarro se reagrupó y tomó la fortaleza.</p><br>
