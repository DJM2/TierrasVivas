                    <h3>Día 02: Wakawasi – Laguna Ipsaycocha – Patacancha</h3>
                    <p class="p-salkantay">
                        Después del desayuno continuaremos nuestra caminata y veremos grandes habilidades de los
                        pobladores en lo que respecta los tejidos y confección de prendas y luego subiremos al paso
                        Ipsaycocha (4,100 m.s.n.m o 13,451 pies) desde ese lugar tendremos vista de la famosa montaña
                        Verónica o Wakayhuillca (5,750 m.s.n.m o 18,865 pies) y en este lugar es donde se arma la carpa
                        para disfrutar el almuerzo continuaremos descendiendo por aproximadamente una hora y media hacia
                        nuestro último campamento que se encuentra en el poblado de Patacancha, es ahí donde tendremos
                        una deliciosa cena.
                    </p>
                    <h3>Día 03: Patacancha – Ollantaytambo – Aguas Calientes</h3>
                    <p class="p-salkantay">
                        Temprano, después del desayuno saldremos con dirección a Ollantaytambo, caminando de descenso
                        llegaremos a un mirador, que nos dará una impresionante vista del valle para llegar al poblado
                        de Huylloc, luego continuaremos caminando para llegar al poblado de Pumamarka y disfrutar de las
                        terrazas incas, terminando el recorrido en Ollantaytambo, disfrutaremos del box lunch preparado
                        por nuestro cocinero. Luego tomaremos el tren con dirección a Machu Picchu Pueblo y pasar la
                        noche en un alojamiento básico.
                    </p>
                    <h3>Día 04: Aguas Calientes – Machu Picchu – Cusco</h3>
                    <p class="p-salkantay">
